// composite-install-extract-link.mjs
//
// HYPOTHESIS TESTED (and mostly REJECTED on 2026-06 baseline — keep this script as the
// Phase 2 regression guard): tarball extraction pays the libuv CRT-fd layer per file —
// extraction opens outputs natively (openat_windows, src/libarchive/lib.rs:2032) but
// converts each HANDLE to a CRT fd via make_lib_uv_owned() (lib.rs:~2080, uv_open_osfhandle)
// and writes/closes through uv-kind fds (plan §2.3); plain-path mkdir/unlink are uv-routed.
//
// MEASURED REALITY (Win11 + Defender, 24-core dev box, 12 pkgs / ~516 files):
//   cold (extract+link) ~= warm (link-only): the extract phase costs ~0 at this scale —
//   the uv CRT-write layer is NOT the bottleneck; per-file node_modules materialization is.
//   SURPRISE: default --backend hardlink ~1.9s vs --backend copyfile ~0.44s (4.4x), while
//   RAW fs.linkSync and fs.copyFileSync both cost ~0.5 ms/op on the same volume — so the
//   installer's hardlink path carries ~3 ms/file of extra per-file work (candidate suspects:
//   uv-routed utimes/chmod per §2.3, retry/exists logic — needs a debug-build syscall trace).
//   That gap is an installer issue, mostly NOT a libuv-removal win; expect Phase 2 to move
//   the cold-vs-warm delta only. The in-tree comment at libarchive/lib.rs:~2095 documents
//   AV close-stalls dominating big installs — kernel/AV cost stays regardless of libuv.
//
// SCENARIOS (100% local/offline: file: tarball deps generated by composite-gen-fixtures):
//   cold-extract+link  : node_modules AND bun cache wiped each run -> untar 12 packages
//                        into the cache + link into node_modules.
//   warm-link          : only node_modules wiped; cache kept -> hardlink-only (control).
//   warm-link-copyfile : same but --backend copyfile (CopyFileW, native already — contrast).
//
// RUN (baseline):       node bench/libuv-removal/composite-install-extract-link.mjs   (or bun)
// RUN (after a phase):  BENCH_BUN=path/to/new-bun.exe node bench/libuv-removal/composite-install-extract-link.mjs
// Knobs: BENCH_RUNS (default 6), BENCH_FAST=1. Numbers are dev-box INDICATIVE (Defender on).

import { spawnSync } from "node:child_process";
import { existsSync, rmSync } from "node:fs";
import { cpus } from "node:os";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const FIX = join(HERE, "fixtures", "install");
const CONSUMER = join(FIX, "consumer");
const CACHE = join(FIX, "bun-cache");
const BUN = process.env.BENCH_BUN || "bun";
const FAST = !!process.env.BENCH_FAST;
const RUNS = +(process.env.BENCH_RUNS || (FAST ? 3 : 6));
const WARMUP = 1;

if (!existsSync(join(CONSUMER, "package.json"))) {
  const r = spawnSync(process.execPath, [join(HERE, "composite-gen-fixtures.mjs")], { stdio: "inherit", windowsHide: true });
  if (r.status !== 0) process.exit(1);
}

const baseEnv = { ...process.env, NO_COLOR: "1", BUN_INSTALL_CACHE_DIR: CACHE };
delete baseEnv.UV_THREADPOOL_SIZE;
delete baseEnv.GOMAXPROCS;

const median = a => { const s = [...a].sort((x, y) => x - y); return s.length % 2 ? s[(s.length - 1) / 2] : (s[s.length / 2 - 1] + s[s.length / 2]) / 2; };
const fmt = (n, w = 8) => n.toFixed(2).padStart(w);

function installOnce(extraArgs, { wipeCache }) {
  rmSync(join(CONSUMER, "node_modules"), { recursive: true, force: true });
  if (wipeCache) rmSync(CACHE, { recursive: true, force: true });
  const t0 = process.hrtime.bigint();
  const r = spawnSync(BUN, ["install", "--no-progress", "--no-summary", ...extraArgs], { cwd: CONSUMER, encoding: "utf8", windowsHide: true, env: baseEnv, stdio: ["ignore", "pipe", "pipe"] });
  const ms = Number(process.hrtime.bigint() - t0) / 1e6;
  if (r.status !== 0) { console.error(`bun install failed:\n${r.stdout}\n${r.stderr}`); process.exit(1); }
  return ms;
}

function scenario(label, extraArgs, opts) {
  for (let i = 0; i < WARMUP; i++) installOnce(extraArgs, opts);
  const times = [];
  for (let i = 0; i < RUNS; i++) times.push(installOnce(extraArgs, opts));
  console.log(`${label.padEnd(42)} ${fmt(median(times))} ms  (min ${fmt(Math.min(...times), 7)}, max ${fmt(Math.max(...times), 7)}, n=${RUNS})`);
  return median(times);
}

console.log(`# composite-install-extract-link | cores=${cpus().length} | runs=${RUNS} (+${WARMUP} warmup) | ${new Date().toISOString()}`);
console.log(`# bun=${BUN} (${spawnSync(BUN, ["--version"], { encoding: "utf8", windowsHide: true }).stdout.trim()}) | 12 local packages, ~516 files, file: tarballs, offline\n`);

// prime lockfile once (untimed) so every measured run is install-from-lockfile
installOnce([], { wipeCache: true });

const cold = scenario("cold: extract+link (cache+node_modules wiped)", [], { wipeCache: true });
const warm = scenario("warm: link-only (cache kept, hardlink)", [], { wipeCache: false });
const copy = scenario("warm: link-only (--backend copyfile)", ["--backend", "copyfile"], { wipeCache: false });

console.log(`\n## derived`);
console.log(`extract-phase cost (cold - warm): ${(cold - warm).toFixed(2)} ms for 12 tarballs / ~516 files`);
console.log(`  -> the slice that contains the uv CRT-fd write/close layer (libarchive extract). ~0 on the`);
console.log(`     2026-06 baseline: install is NOT libuv-bound at this scale; watch this stays ~0 after Phase 2.`);
console.log(`copyfile vs hardlink backends: ${(copy - warm).toFixed(2)} ms (baseline found hardlink ~4x SLOWER —`);
console.log(`  installer-level per-file overhead, raw link/copy syscalls are both ~0.5 ms on this box)`);
