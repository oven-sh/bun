// Generate the CI machine files from the single source of truth (spec.ts).
//
// For every image, writes build/ci/<key>/:
//   bootstrap.ts   the self-contained recipe: this image's resolved entry
//                  + the bootstrap program, bundled by esbuild into one
//                  stdlib-only file that node runs on the machine
//   packer.json    (windows) the Packer template, spec-derived values as
//                  literal text, bake-time values as {{placeholders}}
//   agent.mjs      the runner service, bundled by esbuild
//
// These files ARE the image: what they contain is what gets baked. The
// image name is `${key}-${sha256(these files)}` (naming.ts), so editing
// spec.ts (or the recipe code) changes the bytes here, which changes the
// name, which makes CI bake. A refactor that leaves these files identical
// changes no name and bakes nothing.
//
// Run via `bun scripts/build.ts` (generateCiImages), or directly.

import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { catalogEntry } from "./components/catalog.ts";
import { hashImageDir, imageOutDir } from "./outputs.ts";
import { PACKER_PLACEHOLDERS, windowsPackerTemplate } from "./packer.ts";
import { epoch, images } from "./spec.ts";
import type { Image } from "./types.ts";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", "..", "..");
/** The bundler: the toolchain's resolved esbuild (from configure), else the
 * root install's. */
function esbuildBin(explicit?: string): string {
  const bin =
    explicit ?? join(repoRoot, "node_modules", ".bin", process.platform === "win32" ? "esbuild.exe" : "esbuild");
  if (!existsSync(bin)) {
    throw new Error(`esbuild not found at ${bin}; run \`bun install\` in the repo root first`);
  }
  return bin;
}

/** Bundle one entry point into a single stdlib-only ESM file. */
function bundle(esbuild: string, entry: string, outfile: string): void {
  execFileSync(
    esbuild,
    [entry, "--bundle", "--platform=node", "--format=esm", `--outfile=${outfile}`, "--log-level=warning"],
    {
      cwd: repoRoot,
      stdio: ["ignore", "ignore", "inherit"],
    },
  );
}

/** The per-image entry: this image's resolved entry inlined as a literal,
 * handed to the bootstrap program. Nothing here imports the spec's image
 * list, so the bundle carries only this image. */
function bootstrapEntrySource(image: Image): string {
  const components = join(here, "components");
  // Static imports for exactly this image's components, in order, and (linux
  // only) its one package manager — so the bundle carries only this image's
  // code and esbuild's import-graph tree-shaking drops everything else.
  const imports: string[] = [];
  const modules = new Map<string, Set<string>>();
  for (const name of image.components) {
    const { module, export: exportName } = catalogEntry(name, image.os);
    const path = join(components, module);
    (modules.get(path) ?? modules.set(path, new Set()).get(path)!).add(exportName);
  }
  for (const [path, names] of modules) {
    imports.push(`import { ${[...names].join(", ")} } from ${JSON.stringify(path)};`);
  }
  const componentList = `[${image.components.map(name => catalogEntry(name, image.os).export).join(", ")}]`;
  const manager =
    image.os === "linux"
      ? [
          `import { ${image.packages.manager} } from ${JSON.stringify(join(components, "package-manager.ts"))};`,
          `const manager = ${image.packages.manager};`,
        ]
      : [`const manager = undefined;`];
  return [
    `// GENERATED by scripts/build/ci/generate.ts — do not edit.`,
    `// The self-contained bootstrap for image "${image.key}".`,
    `import { runBootstrap } from ${JSON.stringify(join(here, "bootstrap.ts"))};`,
    ...imports,
    ...manager,
    `const image = ${JSON.stringify(image, null, 2)};`,
    `const components = ${componentList};`,
    `runBootstrap(image, components, ${epoch}, manager, process.argv.slice(2)).catch(error => {`,
    `  console.error("\\nbootstrap: " + (error instanceof Error ? error.message : String(error)));`,
    `  process.exit(1);`,
    `});`,
    ``,
  ].join("\n");
}

/** Generate one image's files. Returns the output directory. */
export function generateImage(image: Image, esbuild?: string): string {
  const bin = esbuildBin(esbuild);
  const dir = imageOutDir(image);
  rmSync(dir, { recursive: true, force: true });
  mkdirSync(dir, { recursive: true });

  const entry = join(dir, "_entry.ts");
  writeFileSync(entry, bootstrapEntrySource(image));
  bundle(bin, entry, join(dir, "bootstrap.ts"));
  rmSync(entry);

  bundle(bin, join(repoRoot, "scripts", "agent.mjs"), join(dir, "agent.mjs"));

  if (image.os === "windows") {
    const template = windowsPackerTemplate({
      image,
      imageName: `${image.key}-{{hash}}`,
      repoRef: PACKER_PLACEHOLDERS.repoRef,
      bootstrapFile: PACKER_PLACEHOLDERS.bootstrapFile,
      agentPath: PACKER_PLACEHOLDERS.agentPath,
      azure: PACKER_PLACEHOLDERS,
    });
    writeFileSync(join(dir, "packer.json"), JSON.stringify(template, null, 2) + "\n");
  }
  return dir;
}

/** Generate every image's files. */
export function generateCiImages(esbuild?: string): void {
  for (const image of images) generateImage(image, esbuild);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])) {
  generateCiImages();
  for (const image of images) {
    console.log(`${image.key}  ${hashImageDir(imageOutDir(image)).slice(0, 16)}`);
  }
}
