/**
 * WebKit DirectBuild — WTF layer.
 *
 * Sources = base list (CMakeLists.txt, platform-independent) + platform
 * additions (PlatformJSCOnly.cmake). data.json's WTF.sources captured the
 * Linux build, so the base list here is that minus anything under a
 * platform subdir; per-platform sets are written out explicitly.
 *
 * No <wtf/X.h> forwarding tree needed — Source/WTF/ mirrors it 1:1, so
 * `-I Source/WTF` is enough. <bmalloc/X.h> comes from webkit-bmalloc's
 * forwardHeaders staging.
 */

import type { Dependency, DirectBuild } from "../../source.ts";
import { depBuildDir } from "../../source.ts";
import { webkitDirectSource } from "./bmalloc.ts";
import { commonDefines, layerData, webkitCFlags, webkitCxxFlags } from "./common.ts";

const layer = layerData.WTF;

// Forwarding-header dirs cmake stages → the source dirs they mirror.
// $BUILD (the cmake root) is where cmakeconfig.h lives; we put that in
// webkit-bmalloc's buildDir instead.
const SRC_INCLUDES = layer.includes.filter(i => i.startsWith("$SRC/")).map(i => i.replace("$SRC/", ""));

// Base sources: data.json's Linux capture minus platform subdirs.
const PLATFORM_SUBDIR = /\/wtf\/(posix|linux|unix|generic|win|cocoa|cf|mac|text\/unix|text\/win|bun)\//;
const BASE_SOURCES = layer.sources.map(s => s.replace("$SRC/", "")).filter(s => !PLATFORM_SUBDIR.test(s));

// ─── Platform additions — mirrors PlatformJSCOnly.cmake ───
// All paths relative to Source/WTF/wtf/.
// prettier-ignore
const POSIX = [
  "generic/WorkQueueGeneric.cpp", "generic/MainThreadGeneric.cpp",
  "posix/OSAllocatorPOSIX.cpp", "posix/ThreadingPOSIX.cpp",
  "posix/CPUTimePOSIX.cpp", "posix/FileHandlePOSIX.cpp",
  "posix/FileSystemPOSIX.cpp", "posix/MappedFileDataPOSIX.cpp",
  "text/unix/TextBreakIteratorInternalICUUnix.cpp",
  "unix/LanguageUnix.cpp", "unix/LoggingUnix.cpp", "unix/UniStdExtrasUnix.cpp",
  // EVENT_LOOP_TYPE = "bun" (we set USE_BUN_EVENT_LOOP in feature-defines).
  "bun/RunLoopBun.cpp",
];

// prettier-ignore
const LINUX = [
  "linux/CurrentProcessMemoryStatus.cpp", "linux/MemoryFootprintLinux.cpp",
  "linux/RealTimeThreads.cpp", "unix/MemoryPressureHandlerUnix.cpp",
];

// prettier-ignore
const DARWIN = [
  "cocoa/MemoryFootprintCocoa.cpp", "generic/MemoryPressureHandlerGeneric.cpp",
  // Generated by `mig` from MachExceptions.defs (see codegen below).
  "$BUILD/mach_excServer.c", "$BUILD/mach_excUser.c",
];

// prettier-ignore
const WINDOWS = [
  "generic/WorkQueueGeneric.cpp", "bun/RunLoopBun.cpp",
  "text/win/StringWin.cpp", "text/win/TextBreakIteratorInternalICUWin.cpp",
  "win/CPUTimeWin.cpp", "win/DbgHelperWin.cpp", "win/FileHandleWin.cpp",
  "win/FileSystemWin.cpp", "win/LanguageWin.cpp", "win/LoggingWin.cpp",
  "win/MainThreadWin.cpp", "win/MappedFileDataWin.cpp", "win/OSAllocatorWin.cpp",
  "win/PathWalker.cpp", "win/SignalsWin.cpp", "win/ThreadingWin.cpp",
  "win/WTFCRTDebug.cpp", "win/Win32Handle.cpp",
  "win/MemoryFootprintWin.cpp", "win/MemoryPressureHandlerWin.cpp",
];

export const webkitWTF: Dependency = {
  name: "wtf",
  enabled: cfg => cfg.webkit === "direct",
  fetchDeps: ["bmalloc"],

  source: webkitDirectSource,

  build: cfg => {
    const platform = cfg.windows ? WINDOWS : cfg.darwin ? [...POSIX, ...DARWIN] : [...POSIX, ...LINUX];
    const spec: DirectBuild = {
      kind: "direct",
      pic: true,
      sources: [...BASE_SOURCES, ...platform.map(s => (s.startsWith("$BUILD/") ? s : `Source/WTF/wtf/${s}`))],
      includes: SRC_INCLUDES,
      defines: {
        ...commonDefines,
        BUILDING_WTF: true,
        PAS_BMALLOC: 1,
        STATICALLY_LINKED_WITH_bmalloc: true,
        ...(cfg.linux && { _GNU_SOURCE: true, _GLIBCXX_ASSERTIONS: 1 }),
      },
      cflags: [...webkitCFlags(cfg), `-I${depBuildDir(cfg, "bmalloc")}`],
      cxxflags: webkitCxxFlags(cfg),
    };

    // macOS: Threading.cpp's signal-based suspend/resume reads the EXC_*
    // mach exception state via the mig-generated server. `mig` is in the
    // Xcode toolchain.
    if (cfg.darwin) {
      spec.codegen = [
        {
          // `mig` (Mach Interface Generator, ships with Xcode) — args are
          // flags-then-file, so use the bare-interpreter form.
          interpreter: "mig",
          args: [
            "-DMACH_EXC_SERVER_TASKIDTOKEN_STATE",
            "-sheader",
            "$BUILD/MachExceptionsServer.h",
            "$SRC/Source/WTF/wtf/mac/MachExceptions.defs",
          ],
          outputs: [
            "$BUILD/MachExceptionsServer.h",
            "$BUILD/mach_exc.h",
            "$BUILD/mach_excServer.c",
            "$BUILD/mach_excUser.c",
          ],
          inputs: ["$SRC/Source/WTF/wtf/mac/MachExceptions.defs"],
          cwd: "$BUILD",
        },
      ];
    }

    return spec;
  },

  provides: () => ({
    libs: [],
    includes: ["Source/WTF"],
  }),
};
