/**
 * Generates `${codegenDir}/json_byte_class.h` and `json_byte_class.rs` — the
 * byte classification used by BOTH producers of the JSON structural index:
 *
 *   - the Highway SIMD kernel (`src/jsc/bindings/highway_json.cpp`) consumes
 *     the two 16-byte nibble LUTs (`cls = lo[b & 0xF] & hi[b >> 4]`,
 *     simdjson's trick: one `TableLookupBytes` pair classifies 64 bytes),
 *   - the Rust scalar indexer (`src/parsers/json_index.rs`) consumes the
 *     derived 256-entry table (`JSON_BYTE_CLASS[b]`, the same expression
 *     evaluated per byte).
 *
 * The nibble compression is exact for ASCII but deliberately false-positives
 * a handful of bytes >= 0x80 as structural (0xBB — the middle byte of a
 * UTF-8 BOM — 0xBC, 0xBD, 0xCC, 0xDB, 0xDD). That is fine for parsing
 * (stage 2 treats them as unexpected tokens / exotic whitespace), but the
 * two producers MUST agree byte for byte: when the kernel bails out on a
 * comment or a single quote, the scalar restart swallows a *count* of
 * already-delivered indices, and the differential tests compare the two
 * streams. Generating both sides from this one definition makes a
 * disagreement structurally impossible.
 *
 * Written at configure time like `buildOptionsRs.ts` — it is a constant
 * manifest, not a build edge.
 */

import { mkdirSync } from "node:fs";
import { resolve } from "node:path";
import type { Config } from "./config.ts";
import { writeIfChanged } from "./fs.ts";

// Class bits (`cls = LUT_LO[b & 0xF] & LUT_HI[b >> 4]`).
const STRUCTURAL = 0x07; // { } [ ] : ,
const WHITESPACE = 0x18; // space \t \n \r
const ODDITY = 0x20; // / '   (comment / single-quoted string: kernel bails)
const CONTROL = 0x40; // < 0x20

// The nibble LUTs, derived from simdjson's structural/whitespace tables with
// the oddity and control classes folded in. THE single source of truth.
const LUT_LO = [0x50, 0x40, 0x40, 0x40, 0x40, 0x40, 0x40, 0x60, 0x40, 0x48, 0x4c, 0x41, 0x42, 0x49, 0x40, 0x60];
const LUT_HI = [0x48, 0x40, 0x32, 0x04, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x03, 0x02, 0x01, 0x00, 0x00];

const classOf = (b: number): number => LUT_LO[b & 0xf] & LUT_HI[b >> 4];

/** The classification must be exact for every ASCII byte. */
function check() {
  // The classes a byte belongs to (a byte can be in several: `\t` is both
  // whitespace and a control character).
  const classesOf = (b: number): number[] => {
    const ch = String.fromCharCode(b);
    const classes: number[] = [];
    if ("{}[]:,".includes(ch)) classes.push(STRUCTURAL);
    if (" \t\n\r".includes(ch)) classes.push(WHITESPACE);
    if ("/'".includes(ch)) classes.push(ODDITY);
    if (b < 0x20) classes.push(CONTROL);
    return classes;
  };
  for (let b = 0; b < 0x80; b++) {
    const got = classOf(b);
    const classes = classesOf(b);
    const allowed = classes.reduce((acc, c) => acc | c, 0);
    // At least one bit of every class the byte is in, and no foreign bits.
    const ok = classes.every(c => (got & c) !== 0) && (got & ~allowed) === 0;
    if (!ok) {
      throw new Error(
        `json_byte_class: byte 0x${b.toString(16)} classifies as 0x${got.toString(16)}, expected classes 0x${allowed.toString(16)}`,
      );
    }
  }
}

export function generateJsonByteClass(cfg: Config): { h: string; rs: string } {
  check();

  const banner = (comment: string) => [
    `${comment} Generated by scripts/build/jsonByteClass.ts at configure time. Do not`,
    `${comment} edit. The same definition feeds the Highway JSON kernel (these nibble`,
    `${comment} LUTs) and the Rust scalar indexer (the derived 256-entry table); see`,
    `${comment} the generator for why they must agree byte for byte.`,
    `${comment}`,
    `${comment} cls = LUT_LO[b & 0xF] & LUT_HI[b >> 4]`,
    `${comment}   0x07 structural { } [ ] : ,    0x18 whitespace sp \\t \\n \\r`,
    `${comment}   0x20 oddity / '                0x40 control < 0x20`,
    "",
  ];
  const hex = (v: number) => `0x${v.toString(16).padStart(2, "0")}`;

  const h = [
    ...banner("//"),
    "#pragma once",
    "#include <stdint.h>",
    "",
    `#define BUN_JSON_CLASS_STRUCTURAL ${hex(STRUCTURAL)}`,
    `#define BUN_JSON_CLASS_WHITESPACE ${hex(WHITESPACE)}`,
    `#define BUN_JSON_CLASS_ODDITY ${hex(ODDITY)}`,
    `#define BUN_JSON_CLASS_CONTROL ${hex(CONTROL)}`,
    "",
    `alignas(16) static const uint8_t kBunJsonLutLo[16] = { ${LUT_LO.map(hex).join(", ")} };`,
    `alignas(16) static const uint8_t kBunJsonLutHi[16] = { ${LUT_HI.map(hex).join(", ")} };`,
    "",
  ].join("\n");

  const table: string[] = [];
  for (let row = 0; row < 256; row += 16) {
    const cells = [];
    for (let b = row; b < row + 16; b++) cells.push(hex(classOf(b)));
    table.push(`    ${cells.join(", ")},`);
  }
  // The scalar indexer needs only these two classes (quotes, `/` comments and
  // control characters are handled by explicit byte matches before the table
  // dispatch), so only these are emitted for Rust: every generated item must
  // be live (`test/internal/dead-code-escapes.test.ts`).
  const rs = [
    ...banner("//"),
    `pub const CLASS_STRUCTURAL: u8 = ${hex(STRUCTURAL)};`,
    `pub const CLASS_WHITESPACE: u8 = ${hex(WHITESPACE)};`,
    "",
    "/// `LUT_LO[b & 0xF] & LUT_HI[b >> 4]` for every byte `b`.",
    "#[rustfmt::skip]",
    "pub const JSON_BYTE_CLASS: [u8; 256] = [",
    ...table,
    "];",
    "",
  ].join("\n");

  mkdirSync(cfg.codegenDir, { recursive: true });
  const hPath = resolve(cfg.codegenDir, "json_byte_class.h");
  const rsPath = resolve(cfg.codegenDir, "json_byte_class.rs");
  writeIfChanged(hPath, h);
  writeIfChanged(rsPath, rs);
  return { h: hPath, rs: rsPath };
}
