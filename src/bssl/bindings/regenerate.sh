#!/usr/bin/env bash
# Regenerates the committed per-target bssl-sys bindings (wrapper_<triple>.rs)
# and the --wrap-static-fns shim (wrapper.c) from vendor/boringssl. Re-run on
# every BoringSSL commit bump (scripts/build/deps/boringssl.ts) — see README.md.
#
# Requires: bindgen-cli (`cargo install bindgen-cli`), a fetched
# vendor/boringssl + vendor/zig (`bun run build --target=clone-boringssl` etc.),
# and for the darwin/windows triples a macOS SDK / MSVC splat:
#   MACOS_SDK   (default: $HOME/.bun/build-cache/MacOSX*.sdk, newest)
#   WINSYSROOT  (default: /opt/winsysroot)
set -euo pipefail

OUT_DIR="$(cd "$(dirname "$0")" && pwd)"
BUN_ROOT="$(cd "$OUT_DIR/../../.." && pwd)"
BORINGSSL="$BUN_ROOT/vendor/boringssl"
ZIG_LIBC="$BUN_ROOT/vendor/zig/lib/libc/include"
RESOURCE_DIR="$(clang -print-resource-dir)/include"

# Stamp every output with the pinned commit; boringssl.ts fails the configure
# if the stamp and BORINGSSL_COMMIT disagree (stale bindings after a bump).
BORINGSSL_COMMIT="$(sed -n 's/^const BORINGSSL_COMMIT = "\([0-9a-f]\{40\}\)";$/\1/p' \
  "$BUN_ROOT/scripts/build/deps/boringssl.ts")"
[[ -n "$BORINGSSL_COMMIT" ]] || {
  echo "error: could not read BORINGSSL_COMMIT from scripts/build/deps/boringssl.ts" >&2
  exit 1
}
STAMP="// BoringSSL commit: $BORINGSSL_COMMIT (stamped by regenerate.sh; verified by scripts/build/deps/boringssl.ts)"

if [[ -z "${MACOS_SDK:-}" ]]; then
  MACOS_SDK="$(ls -d "$HOME"/.bun/build-cache/MacOSX*.sdk 2>/dev/null | sort -V | tail -1 || true)"
fi
WINSYSROOT="${WINSYSROOT:-/opt/winsysroot}"

# Mirrors vendor/boringssl/rust/bssl-sys/CMakeLists.txt bindgen invocation
# (the BINDGEN_RS_FILE escape hatch consumes these outputs verbatim).
BINDGEN_ARGS=(
  --no-derive-default
  --enable-function-attribute-detection
  --use-core
  --default-macro-constant-type=signed
  --rustified-enum=point_conversion_form_t
  --allowlist-file '.*[[:punct:]]include[[:punct:]]openssl[[:punct:]].*\.h'
  --experimental
  --wrap-static-fns
)

gen() {
  local triple="$1"
  shift
  echo "── $triple"
  local wrap_c
  wrap_c="$(mktemp --suffix=.c)"
  (cd "$BORINGSSL" && bindgen rust/bssl-sys/wrapper.h \
    -o "$OUT_DIR/wrapper_$triple.rs" \
    "${BINDGEN_ARGS[@]}" \
    --wrap-static-fns-path "$wrap_c" \
    -- -I "$BORINGSSL/include" --target="$triple" "$@")
  sed -i "1i $STAMP" "$OUT_DIR/wrapper_$triple.rs"
  # Post-process: inline wrapper.h (so the file compiles with only
  # -I vendor/boringssl/include) and add C linkage guards — boringssl.ts
  # compiles the dep with lang:"cxx" (-x c++ for .c files) and the bindings
  # reference the unmangled *__extern symbol names.
  {
    printf '%s\n' "$STAMP"
    printf '// Generated by src/bssl/bindings/regenerate.sh — do not edit.\n'
    printf '// bindgen --wrap-static-fns shims; header list inlined from\n'
    printf '// vendor/boringssl/rust/bssl-sys/wrapper.h.\n'
    grep -v '^//' "$BORINGSSL/rust/bssl-sys/wrapper.h"
    printf '\n#if defined(__cplusplus)\nextern "C" {\n#endif\n'
    tail -n +2 "$wrap_c"
    printf '\n#if defined(__cplusplus)\n}  // extern "C"\n#endif\n'
  } >"$wrap_c.guarded"
  # wrapper.c must be byte-identical across targets — it is compiled once,
  # for every target, by scripts/build/deps/boringssl.ts.
  if [[ -f "$OUT_DIR/wrapper.c" ]]; then
    diff -u "$OUT_DIR/wrapper.c" "$wrap_c.guarded" || {
      echo "error: wrapper.c differs for $triple — boringssl.ts must go per-target" >&2
      exit 1
    }
  else
    cp "$wrap_c.guarded" "$OUT_DIR/wrapper.c"
  fi
  rm -f "$wrap_c" "$wrap_c.guarded"
}

# Hermetic libc headers: zig's bundled set for linux (gnu + musl), the macOS
# SDK for darwin, the MSVC/UCRT splat for windows.
linux_gnu() {
  local triple="$1" arch="$2"
  gen "$triple" -nostdinc \
    -isystem "$RESOURCE_DIR" \
    -isystem "$ZIG_LIBC/$arch-linux-gnu" \
    -isystem "$ZIG_LIBC/generic-glibc" \
    -isystem "$ZIG_LIBC/$arch-linux-any" \
    -isystem "$ZIG_LIBC/any-linux-any"
}
linux_musl() {
  local triple="$1" arch="$2"
  gen "$triple" -nostdinc \
    -isystem "$RESOURCE_DIR" \
    -isystem "$ZIG_LIBC/$arch-linux-musl" \
    -isystem "$ZIG_LIBC/generic-musl" \
    -isystem "$ZIG_LIBC/$arch-linux-any" \
    -isystem "$ZIG_LIBC/any-linux-any"
}
freebsd() {
  local triple="$1" arch="$2"
  gen "$triple" -nostdinc \
    -isystem "$RESOURCE_DIR" \
    -isystem "$ZIG_LIBC/$arch-freebsd-none" \
    -isystem "$ZIG_LIBC/generic-freebsd"
}
# zig bundles no bionic headers; musl's are the closest available. The only
# libc types that leak into the OpenSSL API surface (FILE*, struct tm,
# struct timeval, time_t) have identical shapes in bionic and musl.
android() {
  local triple="$1" arch="$2"
  gen "$triple" -nostdinc \
    -isystem "$RESOURCE_DIR" \
    -isystem "$ZIG_LIBC/$arch-linux-musl" \
    -isystem "$ZIG_LIBC/generic-musl" \
    -isystem "$ZIG_LIBC/$arch-linux-any" \
    -isystem "$ZIG_LIBC/any-linux-any"
}
darwin() {
  gen "$1" -isysroot "$MACOS_SDK"
}
windows() {
  gen "$1" -nostdinc \
    -isystem "$RESOURCE_DIR" \
    -isystem "$WINSYSROOT/VC/Tools/MSVC"/*/include \
    -isystem "$WINSYSROOT/Windows Kits/10/Include"/*/ucrt
}

rm -f "$OUT_DIR/wrapper.c"
linux_gnu x86_64-unknown-linux-gnu x86
linux_gnu aarch64-unknown-linux-gnu aarch64
linux_musl x86_64-unknown-linux-musl x86_64
linux_musl aarch64-unknown-linux-musl aarch64
freebsd x86_64-unknown-freebsd x86_64
freebsd aarch64-unknown-freebsd aarch64
android aarch64-linux-android aarch64
android x86_64-linux-android x86_64
darwin x86_64-apple-darwin
darwin aarch64-apple-darwin
windows x86_64-pc-windows-msvc
windows aarch64-pc-windows-msvc
echo "done: $(ls "$OUT_DIR"/wrapper_*.rs | wc -l) binding files + wrapper.c"
