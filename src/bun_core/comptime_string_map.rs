//! Static string→value maps with length-dispatched lookups.
//!
//! Rust port of Zig's `ComptimeStringMap`. Declared via
//! [`comptime_string_map!`] / [`comptime_string_set!`]; lookups compile to a
//! `match key.len()` jump table plus constant-length byte compares (word-sized
//! loads against immediates) — no hashing at runtime.
//!
//! ```ignore
//! bun_core::comptime_string_map! {
//!     pub static MODULE_TYPE_MAP: ModuleType = {
//!         b"module" => ModuleType::Module,
//!         b"commonjs" => ModuleType::CommonJs,
//!     };
//! }
//!
//! MODULE_TYPE_MAP.get(bytes).copied()
//! ```
//!
//! String literals (`"key"`) and byte-string literals (`b"key"`) are both
//! accepted as keys. The generated static exposes `get`, `contains_key`,
//! `entries`/`keys`/`values`, `get_with_eql` (custom comparator, e.g.
//! `bun_core::String` UTF-16-aware equality), and
//! `get_ascii_case_insensitive` (keys must be declared lowercase).

#[doc(hidden)]
pub use bun_core_macros::{comptime_string_map_impl, comptime_string_set_impl};

/// Implemented by every type generated by [`comptime_string_map!`]. Lets
/// helpers (JS-value lookups, case-insensitive probes) accept any map
/// generically while each call still inlines the map's own compare tree.
pub trait ComptimeStringMap: Sync + 'static {
    type Value: 'static;

    fn lookup(&self, key: &[u8]) -> Option<&'static Self::Value>;

    /// ASCII-lowercases `key` before the lookup; keys must be declared
    /// lowercase.
    fn lookup_ascii_case_insensitive(&self, key: &[u8]) -> Option<&'static Self::Value>;
}

/// Logical length of a lookup input, in the units the comparator passed to
/// `get_with_eql` compares (bytes for byte slices, code units for UTF-16).
pub trait HasLength {
    fn length(&self) -> usize;
}

impl HasLength for [u8] {
    #[inline]
    fn length(&self) -> usize {
        self.len()
    }
}

impl HasLength for &[u8] {
    #[inline]
    fn length(&self) -> usize {
        self.len()
    }
}

impl HasLength for &str {
    #[inline]
    fn length(&self) -> usize {
        self.len()
    }
}

impl HasLength for &crate::String {
    #[inline]
    fn length(&self) -> usize {
        crate::String::length(self)
    }
}

#[macro_export]
macro_rules! comptime_string_map {
    ($($t:tt)*) => {
        $crate::comptime_string_map::comptime_string_map_impl! {
            @crate_path($crate)
            $($t)*
        }
    };
}

#[macro_export]
macro_rules! comptime_string_set {
    ($($t:tt)*) => {
        $crate::comptime_string_map::comptime_string_set_impl! {
            @crate_path($crate)
            $($t)*
        }
    };
}

#[cfg(test)]
mod tests {
    use super::ComptimeStringMap as ComptimeStringMapTrait;

    #[derive(Copy, Clone, PartialEq, Eq, Debug)]
    enum TestEnum {
        A,
        B,
        C,
        D,
        E,
    }

    crate::comptime_string_map! {
        /// Doc comments are carried through.
        static MAP: TestEnum = {
            b"these" => TestEnum::D,
            b"have" => TestEnum::A,
            "nothing" => TestEnum::B,
            b"incommon" => TestEnum::C,
            b"samelen" => TestEnum::E,
        };
    }

    #[test]
    fn get_hits_and_misses() {
        assert_eq!(MAP.get(b"have"), Some(&TestEnum::A));
        assert_eq!(MAP.get(b"nothing"), Some(&TestEnum::B));
        assert_eq!(MAP.get(b"incommon"), Some(&TestEnum::C));
        assert_eq!(MAP.get(b"these"), Some(&TestEnum::D));
        assert_eq!(MAP.get(b"samelen"), Some(&TestEnum::E));
        assert_eq!(MAP.get(b"missing"), None);
        assert_eq!(MAP.get(b"sameleN"), None);
        assert_eq!(MAP.get(b""), None);
        assert_eq!(MAP.get(b"a-very-long-key-beyond-max-len"), None);
        assert!(MAP.contains_key(b"these"));
        assert!(!MAP.contains_key(b"thesE"));
    }

    #[test]
    fn bounds_and_entries() {
        assert_eq!(<__ComptimeStringMap_MAP>::MIN_LEN, 4);
        assert_eq!(<__ComptimeStringMap_MAP>::MAX_LEN, 8);
        assert_eq!(MAP.len(), 5);
        // Declaration order is preserved.
        let entries: Vec<_> = MAP.entries().collect();
        assert_eq!(entries[0], (b"these".as_slice(), &TestEnum::D));
        assert_eq!(entries[2], (b"nothing".as_slice(), &TestEnum::B));
        assert_eq!(MAP.keys().count(), 5);
        assert_eq!(MAP.values().count(), 5);
    }

    #[test]
    fn ascii_case_insensitive() {
        assert_eq!(MAP.get_ascii_case_insensitive(b"THESE"), Some(&TestEnum::D));
        assert_eq!(
            MAP.get_ascii_case_insensitive(b"SameLen"),
            Some(&TestEnum::E)
        );
        assert_eq!(MAP.get_ascii_case_insensitive(b"MISSING"), None);
        assert_eq!(MAP.get_ascii_case_insensitive(b""), None);
        assert_eq!(
            MAP.get_ascii_case_insensitive(b"longer-than-max-len-key"),
            None
        );
    }

    #[test]
    fn custom_eql() {
        // Comparator that treats '_' in the input as matching '-' won't be
        // exercised here; just prove an arbitrary comparator drives lookup.
        let found = MAP.get_with_eql(b"these".as_slice(), |a: &[u8], b| a == b);
        assert_eq!(found, Some(&TestEnum::D));
        let none = MAP.get_with_eql(b"zzzzz".as_slice(), |a: &[u8], b| a == b);
        assert_eq!(none, None);
    }

    #[test]
    fn through_trait() {
        fn generic_lookup<M: ComptimeStringMapTrait>(
            map: &M,
            key: &[u8],
        ) -> Option<&'static M::Value> {
            map.lookup(key)
        }
        assert_eq!(generic_lookup(&MAP, b"have"), Some(&TestEnum::A));
        assert_eq!(generic_lookup(&MAP, b"nope"), None);
    }

    #[test]
    fn str_value_map() {
        crate::comptime_string_map! {
            static ALIASES: &'static str = {
                "ls" => "list",
                "rm" => "remove",
            };
        }
        assert_eq!(ALIASES.get(b"ls"), Some(&"list"));
        assert_eq!(ALIASES.get(b"mv"), None);
    }

    #[test]
    fn set_form() {
        crate::comptime_string_set! {
            static COMMANDS = {
                b"install",
                "remove",
                b"why",
            };
        }
        assert!(COMMANDS.contains(b"install"));
        assert!(COMMANDS.contains(b"remove"));
        assert!(COMMANDS.contains(b"why"));
        assert!(!COMMANDS.contains(b"wh"));
        assert!(!COMMANDS.contains(b"whyy"));
        assert!(!COMMANDS.contains(b""));
        assert_eq!(COMMANDS.len(), 3);
        assert_eq!(COMMANDS.iter().count(), 3);
    }
}
