use std::io::Write as _;

use bun_logger as logger;

use crate::zig_string::ZigString;
use crate::{CallFrame, JSGlobalObject, JSValue, JsClass, JsResult, StringJsc as _, ZigStringJsc as _};

#[crate::JsClass] // codegen: JSBuildMessage (toJS / fromJS / fromJSDirect wired by derive)
pub struct BuildMessage {
    pub msg: logger::Msg,
    // resolve_result: Resolver.Result,
    // PORT NOTE: `std.mem.Allocator param` field dropped — global mimalloc.
    pub logged: bool,
}

impl Default for BuildMessage {
    fn default() -> Self {
        Self { msg: logger::Msg::default(), logged: false }
    }
}

impl BuildMessage {
    // `#[JsClass]` emits `BuildMessageClass__construct` calling this.
    pub fn constructor(global: &JSGlobalObject, _frame: &CallFrame) -> JsResult<*mut BuildMessage> {
        Err(global.throw(format_args!("BuildMessage is not constructable")))
    }

    #[crate::host_fn(getter)]
    pub fn get_notes(&self, global: &JSGlobalObject) -> JsResult<JSValue> {
        let notes = &self.msg.notes;
        let array = JSValue::create_empty_array(global, notes.len())?;
        for (i, note) in notes.iter().enumerate() {
            let cloned = note.clone();
            array.put_index(
                global,
                u32::try_from(i).expect("int cast"),
                BuildMessage::create(
                    global,
                    logger::Msg { data: cloned, kind: logger::Kind::Note, ..Default::default() },
                )?,
            )?;
        }

        Ok(array)
    }

    pub fn to_string_fn(&mut self, global: &JSGlobalObject) -> JSValue {
        // std.fmt.allocPrint → write! into Vec<u8>; Rust aborts on OOM so the
        // `catch { throwOutOfMemoryValue }` branch is unreachable here.
        let mut text: Vec<u8> = Vec::new();
        write!(&mut text, "BuildMessage: {}", bstr::BStr::new(&self.msg.data.text)).expect("infallible: in-memory write");

        let mut str = ZigString::init(&text);
        str.set_output_encoding();
        if str.is_utf8() {
            let out = str.to_js(global);
            // default_allocator.free(text) → `text` drops on return.
            return out;
        }

        // All-ASCII path: hand the buffer to JSC as an external Latin-1 string.
        // Ownership transfers via `heap::alloc`; the external-string finalizer
        // calls `mi_free` on the block (global allocator is mimalloc).
        let len = text.len();
        let ptr = bun_core::heap::into_raw(text.into_boxed_slice()).cast::<u8>();
        // SAFETY: ptr/len describe a contiguous mimalloc-owned buffer just
        // released by `heap::alloc`; it stays live until JSC frees it.
        let mut str = ZigString::init(unsafe { bun_core::ffi::slice(ptr, len) });
        str.set_output_encoding();
        str.to_external_value(global)
    }

    /// Spec `BuildMessage.create` (BuildMessage.zig:46) — clone `msg` into a
    /// fresh heap-allocated `BuildMessage` and wrap it in its JSC cell. The
    /// `JsClass::to_js` impl boxes `self` and calls the C++-side
    /// `BuildMessage__create(global, ptr)` (generated by `.classes.ts`); the
    /// resulting `m_ctx` is freed by the macro-emitted
    /// `BuildMessageClass__finalize` on lazy sweep.
    pub fn create(
        global: &JSGlobalObject,
        msg: logger::Msg,
        // resolve_result: *const Resolver.Result,
    ) -> JsResult<JSValue> {
        let build_error = BuildMessage {
            msg: msg.clone(),
            // resolve_result: resolve_result.*,
            logged: false,
        };

        Ok(build_error.to_js(global))
    }

    #[crate::host_fn(method)]
    pub fn to_string(
        &mut self,
        global: &JSGlobalObject,
        _frame: &CallFrame,
    ) -> JsResult<JSValue> {
        Ok(self.to_string_fn(global))
    }

    #[crate::host_fn(method)]
    pub fn to_primitive(
        &mut self,
        global: &JSGlobalObject,
        callframe: &CallFrame,
    ) -> JsResult<JSValue> {
        let args_ = callframe.arguments_old::<1>();
        let args = &args_.ptr[0..args_.len];
        if !args.is_empty() {
            if !args[0].is_string() {
                return Ok(JSValue::NULL);
            }

            let str = args[0].get_zig_string(global)?;
            if str.eql_comptime(b"default") || str.eql_comptime(b"string") {
                return Ok(self.to_string_fn(global));
            }
        }

        Ok(JSValue::NULL)
    }

    #[crate::host_fn(method)]
    pub fn to_json(
        &mut self,
        global: &JSGlobalObject,
        _frame: &CallFrame,
    ) -> JsResult<JSValue> {
        let object = JSValue::create_empty_object(global, 4);
        object.put(global, b"name", bun_string::String::static_str(b"BuildMessage").to_js(global)?);
        object.put(global, b"position", self.get_position(global)?);
        object.put(global, b"message", self.get_message(global)?);
        object.put(global, b"level", self.get_level(global)?);
        Ok(object)
    }

    pub fn generate_position_object(msg: &logger::Msg, global: &JSGlobalObject) -> JSValue {
        let Some(location) = &msg.data.location else { return JSValue::NULL };
        let object = JSValue::create_empty_object(global, 7);

        object.put(
            global,
            b"lineText",
            ZigString::init(location.line_text.as_deref().unwrap_or(b"")).to_js(global),
        );
        object.put(global, b"file", ZigString::init(&location.file).to_js(global));
        object.put(global, b"namespace", ZigString::init(location.namespace).to_js(global));
        object.put(global, b"line", JSValue::from(location.line));
        object.put(global, b"column", JSValue::from(location.column));
        object.put(global, b"length", JSValue::from(location.length));
        object.put(global, b"offset", JSValue::from(location.offset));

        object
    }

    // https://github.com/oven-sh/bun/issues/2375#issuecomment-2121530202
    #[crate::host_fn(getter)]
    pub fn get_column(&self, _global: &JSGlobalObject) -> JsResult<JSValue> {
        if let Some(location) = &self.msg.data.location {
            return Ok(JSValue::from((location.column - 1).max(0)));
        }

        Ok(JSValue::from(0i32))
    }

    #[crate::host_fn(getter)]
    pub fn get_line(&self, _global: &JSGlobalObject) -> JsResult<JSValue> {
        if let Some(location) = &self.msg.data.location {
            return Ok(JSValue::from((location.line - 1).max(0)));
        }

        Ok(JSValue::from(0i32))
    }

    #[crate::host_fn(getter)]
    pub fn get_position(&self, global: &JSGlobalObject) -> JsResult<JSValue> {
        Ok(BuildMessage::generate_position_object(&self.msg, global))
    }

    #[crate::host_fn(getter)]
    pub fn get_message(&self, global: &JSGlobalObject) -> JsResult<JSValue> {
        Ok(ZigString::init(&self.msg.data.text).to_js(global))
    }

    #[crate::host_fn(getter)]
    pub fn get_level(&self, global: &JSGlobalObject) -> JsResult<JSValue> {
        Ok(ZigString::init(self.msg.kind.string()).to_js(global))
    }

    pub fn finalize(self: Box<Self>) {
        drop(self);
    }
}

