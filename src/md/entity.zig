/// HTML named entity lookup.
/// Sorted array of entity names (including & and ;) mapped to Unicode codepoint(s).
pub const Entity = struct {
    name: []const u8,
    codepoints: [2]u21,
};

/// Look up an HTML entity by name (including & prefix and ; suffix).
/// Uses binary search on the sorted entity map.
pub fn lookup(name: []const u8) ?[2]u21 {
    var low: usize = 0;
    var high: usize = entity_map.len;
    while (low < high) {
        const mid = low + (high - low) / 2;
        const cmp = orderStrings(entity_map[mid].name, name);
        if (cmp == .lt) {
            low = mid + 1;
        } else if (cmp == .gt) {
            high = mid;
        } else {
            return entity_map[mid].codepoints;
        }
    }
    return null;
}

fn orderStrings(a: []const u8, b: []const u8) std.math.Order {
    const len = @min(a.len, b.len);
    for (a[0..len], b[0..len]) |ca, cb| {
        if (ca < cb) return .lt;
        if (ca > cb) return .gt;
    }
    return std.math.order(a.len, b.len);
}

