/// `bun.ptr.RefCount(@This(), "ref_count", deinit, .{})` — intrusive single-thread refcount.
/// `ref`/`deref` are provided by `bun_ptr::IntrusiveRc<NativeZlib>`; when the count hits
/// zero it invokes [`NativeZlib::deinit`].
#[bun_jsc::JsClass]
pub struct NativeZlib {
    pub ref_count: Cell<u32>,
    // TODO(port): lifetime — JSC_BORROW backref; global outlives this m_ctx payload.
    pub global_this: *mut JSGlobalObject,
    pub stream: Context,
    pub write_result: Option<*mut u32>,
    pub poll_ref: CountedKeepAlive,
    pub this_value: Strong, // jsc.Strong.Optional
    pub write_in_progress: bool,
    pub pending_close: bool,
    pub pending_reset: bool,
    pub closed: bool,
    pub task: WorkPoolTask,
}

// `const impl = CompressionStream(@This())` — Zig comptime mixin that injects
// write / runFromJSThread / writeSync / reset / close / setOnError / getOnError /
// finalize onto this type. In Rust these are blanket-provided by
// `impl CompressionStream for NativeZlib` in node_zlib_binding.rs.
// TODO(port): verify CompressionStream trait surface matches the Zig mixin re-exports.
impl CompressionStream for NativeZlib {}

impl NativeZlib {
    #[bun_jsc::host_fn]
    pub fn constructor(global: &JSGlobalObject, frame: &CallFrame) -> JsResult<Box<Self>> {
        let arguments = frame.arguments_undef::<4>();

        let mode = arguments[0];
        if !mode.is_number() {
            return Err(global.throw_invalid_argument_type_value("mode", "number", mode));
        }
        let mode_double = mode.as_number();
        if mode_double % 1.0 != 0.0 {
            return Err(global.throw_invalid_argument_type_value("mode", "integer", mode));
        }
        let mode_int: i64 = mode_double as i64;
        if mode_int < 1 || mode_int > 7 {
            return Err(global.throw_range_error(mode_int, bun_jsc::RangeErrorOptions {
                field_name: "mode",
                min: Some(1),
                max: Some(7),
            }));
        }

        let mut ptr = Box::new(Self {
            ref_count: Cell::new(1),
            global_this: global as *const _ as *mut JSGlobalObject,
            stream: Context::default(),
            write_result: None,
            poll_ref: CountedKeepAlive::default(),
            this_value: Strong::empty(),
            write_in_progress: false,
            pending_close: false,
            pending_reset: false,
            closed: false,
            task: WorkPoolTask { callback: None },
        });
        // SAFETY: mode_int is range-checked to 1..=7 above; NodeMode is #[repr(i64-compatible)].
        ptr.stream.mode = unsafe { mem::transmute::<u8, c::NodeMode>(u8::try_from(mode_int).unwrap()) };
        // TODO(port): @enumFromInt — confirm NodeMode repr width matches transmute above.
        Ok(ptr)
    }

    //// adding this didnt help much but leaving it here to compare the number with later
    pub fn estimated_size(&self) -> usize {
        // @sizeOf(@cImport(@cInclude("deflate.h")).internal_state) @ cloudflare/zlib @ 92530568d2c128b4432467b76a3b54d93d6350bd
        const INTERNAL_STATE_SIZE: usize = 3309;
        mem::size_of::<Self>() + INTERNAL_STATE_SIZE
    }

    #[bun_jsc::host_fn(method)]
    pub fn init(&mut self, global: &JSGlobalObject, frame: &CallFrame) -> JsResult<JSValue> {
        let arguments = frame.arguments_undef::<7>();
        let this_value = frame.this();

        if arguments.len() != 7 {
            return Err(global
                .err(bun_jsc::ErrorCode::MISSING_ARGS)
                .fmt(format_args!(
                    "init(windowBits, level, memLevel, strategy, writeResult, writeCallback, dictionary)"
                ))
                .throw());
        }

        let window_bits = validators::validate_int32(global, arguments[0], "windowBits", Default::default(), None, None)?;
        let level = validators::validate_int32(global, arguments[1], "level", Default::default(), None, None)?;
        let mem_level = validators::validate_int32(global, arguments[2], "memLevel", Default::default(), None, None)?;
        let strategy = validators::validate_int32(global, arguments[3], "strategy", Default::default(), None, None)?;
        // this does not get gc'd because it is stored in the JS object's `this._writeState`. and the JS object is tied to the native handle as `_handle[owner_symbol]`.
        let write_result = arguments[4]
            .as_array_buffer(global)
            .unwrap()
            .as_u32()
            .as_mut_ptr();
        let write_callback = validators::validate_function(global, "writeCallback", arguments[5])?;
        let dictionary = if arguments[6].is_undefined() {
            None
        } else {
            Some(arguments[6].as_array_buffer(global).unwrap().byte_slice())
        };

        self.write_result = Some(write_result);
        js::write_callback_set_cached(this_value, global, write_callback.with_async_context_if_needed(global));

        // Keep the dictionary alive by keeping a reference to it in the JS object.
        if dictionary.is_some() {
            js::dictionary_set_cached(this_value, global, arguments[6]);
        }

        self.stream.init(level, window_bits, mem_level, strategy, dictionary);

        Ok(JSValue::UNDEFINED)
    }

    #[bun_jsc::host_fn(method)]
    pub fn params(&mut self, global: &JSGlobalObject, frame: &CallFrame) -> JsResult<JSValue> {
        let arguments = frame.arguments_undef::<2>();

        if arguments.len() != 2 {
            return Err(global
                .err(bun_jsc::ErrorCode::MISSING_ARGS)
                .fmt(format_args!("params(level, strategy)"))
                .throw());
        }

        let level = validators::validate_int32(global, arguments[0], "level", Default::default(), None, None)?;
        let strategy = validators::validate_int32(global, arguments[1], "strategy", Default::default(), None, None)?;

        let err = self.stream.set_params(level, strategy);
        if err.is_error() {
            <Self as CompressionStream>::emit_error(self, global, frame.this(), err);
        }
        Ok(JSValue::UNDEFINED)
    }

    /// RefCount destroy callback. Invoked when `ref_count` reaches zero.
    /// Not `Drop` because this is an intrusive-refcounted `m_ctx` payload whose
    /// box is freed here (`bun.destroy(this)` in Zig).
    fn deinit(this: *mut Self) {
        // SAFETY: called exactly once by IntrusiveRc when refcount hits 0; `this`
        // is the Box::into_raw pointer produced at construction. `this_value`
        // (Strong) and `poll_ref` (CountedKeepAlive) are Drop types — freed by
        // Box::from_raw below.
        unsafe {
            (*this).stream.close();
            drop(Box::from_raw(this));
        }
    }
}

pub struct Context {
    pub mode: c::NodeMode,
    pub state: c::z_stream,
    pub err: c::ReturnCode,
    pub flush: c::FlushValue,
    // TODO(port): lifetime — borrows a JS ArrayBuffer kept alive via
    // js::dictionary_set_cached (BACKREF/FFI class). Stored raw to avoid a
    // 'static lifetime lie; default is the empty static slice.
    pub dictionary: *const [u8],
    pub gzip_id_bytes_read: u8,
}

impl Default for Context {
    fn default() -> Self {
        Self {
            mode: c::NodeMode::NONE,
            // SAFETY: all-zero is a valid z_stream (C struct, std.mem.zeroes in Zig).
            state: unsafe { mem::zeroed::<c::z_stream>() },
            err: c::ReturnCode::Ok,
            flush: c::FlushValue::NoFlush,
            dictionary: b"" as *const [u8],
            gzip_id_bytes_read: 0,
        }
    }
}

impl Context {
    const GZIP_HEADER_ID1: u8 = 0x1f;
    const GZIP_HEADER_ID2: u8 = 0x8b;

    #[inline]
    fn dictionary(&self) -> &[u8] {
        // SAFETY: backing ArrayBuffer is rooted via js::dictionary_set_cached for
        // the lifetime of the JS wrapper, which strictly outlives this Context;
        // default value is `b""` (static).
        unsafe { &*self.dictionary }
    }

    pub fn init(
        &mut self,
        level: c_int,
        window_bits: c_int,
        mem_level: c_int,
        strategy: c_int,
        dictionary: Option<&[u8]>,
    ) {
        use c::NodeMode::*;
        self.flush = c::FlushValue::NoFlush;
        self.err = c::ReturnCode::Ok;

        let window_bits_actual = match self.mode {
            NONE => unreachable!(),
            DEFLATE | INFLATE => window_bits,
            GZIP | GUNZIP => window_bits + 16,
            UNZIP => window_bits + 32,
            DEFLATERAW | INFLATERAW => window_bits * -1,
            BROTLI_DECODE | BROTLI_ENCODE => unreachable!(),
            ZSTD_COMPRESS | ZSTD_DECOMPRESS => unreachable!(),
        };

        // TODO(port): lifetime — see field comment on `dictionary`.
        self.dictionary = match dictionary {
            Some(d) => d as *const [u8],
            None => b"" as *const [u8],
        };

        match self.mode {
            NONE => unreachable!(),
            DEFLATE | GZIP | DEFLATERAW => {
                self.err = c::deflateInit2_(
                    &mut self.state,
                    level,
                    8,
                    window_bits_actual,
                    mem_level,
                    strategy,
                    c::zlibVersion(),
                    c_int::try_from(mem::size_of::<c::z_stream>()).unwrap(),
                );
            }
            INFLATE | GUNZIP | UNZIP | INFLATERAW => {
                self.err = c::inflateInit2_(
                    &mut self.state,
                    window_bits_actual,
                    c::zlibVersion(),
                    c_int::try_from(mem::size_of::<c::z_stream>()).unwrap(),
                );
            }
            BROTLI_DECODE | BROTLI_ENCODE => unreachable!(),
            ZSTD_COMPRESS | ZSTD_DECOMPRESS => unreachable!(),
        }
        if self.err != c::ReturnCode::Ok {
            self.mode = NONE;
            return;
        }

        let _ = self.set_dictionary();
    }

    pub fn set_dictionary(&mut self) -> Error {
        use c::NodeMode::*;
        let dict = self.dictionary();
        if dict.is_empty() {
            return Error::ok();
        }
        self.err = c::ReturnCode::Ok;
        match self.mode {
            DEFLATE | DEFLATERAW => {
                self.err = c::deflateSetDictionary(
                    &mut self.state,
                    dict.as_ptr(),
                    u32::try_from(dict.len()).unwrap(),
                );
            }
            INFLATERAW => {
                self.err = c::inflateSetDictionary(
                    &mut self.state,
                    dict.as_ptr(),
                    u32::try_from(dict.len()).unwrap(),
                );
            }
            _ => {}
        }
        if self.err != c::ReturnCode::Ok {
            return self.error_for_message(c"Failed to set dictionary");
        }
        Error::ok()
    }

    pub fn set_params(&mut self, level: c_int, strategy: c_int) -> Error {
        use c::NodeMode::*;
        self.err = c::ReturnCode::Ok;
        match self.mode {
            DEFLATE | DEFLATERAW => {
                self.err = c::deflateParams(&mut self.state, level, strategy);
            }
            _ => {}
        }
        if self.err != c::ReturnCode::Ok && self.err != c::ReturnCode::BufError {
            return self.error_for_message(c"Failed to set parameters");
        }
        Error::ok()
    }

    fn error_for_message(&self, default: &'static core::ffi::CStr) -> Error {
        let mut message: *const c_char = default.as_ptr();
        if !self.state.err_msg.is_null() {
            message = self.state.err_msg;
        }
        Error {
            msg: message,
            err: self.err as c_int,
            code: match self.err {
                c::ReturnCode::Ok => b"Z_OK",
                c::ReturnCode::StreamEnd => b"Z_STREAM_END",
                c::ReturnCode::NeedDict => b"Z_NEED_DICT",
                c::ReturnCode::ErrNo => b"Z_ERRNO",
                c::ReturnCode::StreamError => b"Z_STREAM_ERROR",
                c::ReturnCode::DataError => b"Z_DATA_ERROR",
                c::ReturnCode::MemError => b"Z_MEM_ERROR",
                c::ReturnCode::BufError => b"Z_BUF_ERROR",
                c::ReturnCode::VersionError => b"Z_VERSION_ERROR",
            },
        }
    }

    pub fn reset(&mut self) -> Error {
        use c::NodeMode::*;
        self.err = c::ReturnCode::Ok;
        match self.mode {
            DEFLATE | DEFLATERAW | GZIP => {
                self.err = c::deflateReset(&mut self.state);
            }
            INFLATE | INFLATERAW | GUNZIP => {
                self.err = c::inflateReset(&mut self.state);
            }
            _ => {}
        }
        if self.err != c::ReturnCode::Ok {
            return self.error_for_message(c"Failed to reset stream");
        }
        self.set_dictionary()
    }

    pub fn set_buffers(&mut self, in_: Option<&[u8]>, out: Option<&mut [u8]>) {
        self.state.avail_in = match &in_ {
            Some(p) => u32::try_from(p.len()).unwrap(),
            None => 0,
        };
        self.state.next_in = match in_ {
            Some(p) => p.as_ptr() as *mut u8,
            None => core::ptr::null_mut(),
        };
        self.state.avail_out = match &out {
            Some(p) => u32::try_from(p.len()).unwrap(),
            None => 0,
        };
        self.state.next_out = match out {
            Some(p) => p.as_mut_ptr(),
            None => core::ptr::null_mut(),
        };
    }

    pub fn set_flush(&mut self, flush: c_int) {
        // SAFETY: caller passes a valid zlib flush constant; FlushValue is #[repr(c_int)].
        self.flush = unsafe { mem::transmute::<c_int, c::FlushValue>(flush) };
    }

    pub fn do_work(&mut self) {
        use c::NodeMode::*;
        let mut next_expected_header_byte: Option<*const u8> = None;

        // If the avail_out is left at 0, then it means that it ran out
        // of room.  If there was avail_out left over, then it means
        // that all of the input was consumed.
        match self.mode {
            DEFLATE | GZIP | DEFLATERAW => {
                return self.do_work_deflate();
            }
            UNZIP => {
                if self.state.avail_in > 0 {
                    next_expected_header_byte = Some(self.state.next_in as *const u8);
                }
                if self.gzip_id_bytes_read == 0 {
                    let Some(p) = next_expected_header_byte else {
                        return self.do_work_inflate();
                    };
                    // SAFETY: avail_in > 0 was checked above, so next_in points to ≥1 byte.
                    if unsafe { *p } == Self::GZIP_HEADER_ID1 {
                        self.gzip_id_bytes_read = 1;
                        // SAFETY: advancing within the input buffer; only dereferenced
                        // below after confirming avail_in > 1.
                        next_expected_header_byte = Some(unsafe { p.add(1) });
                        if self.state.avail_in == 1 {
                            // The only available byte was already read.
                            return self.do_work_inflate();
                        }
                    } else {
                        self.mode = INFLATE;
                        return self.do_work_inflate();
                    }
                }
                if self.gzip_id_bytes_read == 1 {
                    let Some(p) = next_expected_header_byte else {
                        return self.do_work_inflate();
                    };
                    // SAFETY: either avail_in > 1 (fallthrough above) or avail_in > 0
                    // on a fresh call with gzip_id_bytes_read == 1.
                    if unsafe { *p } == Self::GZIP_HEADER_ID2 {
                        self.gzip_id_bytes_read = 2;
                        self.mode = GUNZIP;
                    } else {
                        self.mode = INFLATE;
                    }
                    return self.do_work_inflate();
                }
                debug_assert!(false); // invalid number of gzip magic number bytes read
            }
            INFLATE | GUNZIP | INFLATERAW => {
                return self.do_work_inflate();
            }
            NONE => {}
            BROTLI_ENCODE | BROTLI_DECODE => {}
            ZSTD_COMPRESS | ZSTD_DECOMPRESS => {}
        }
    }

    fn do_work_deflate(&mut self) {
        self.err = c::deflate(&mut self.state, self.flush);
    }

    fn do_work_inflate(&mut self) {
        self.err = c::inflate(&mut self.state, self.flush);

        if self.mode != c::NodeMode::INFLATERAW
            && self.err == c::ReturnCode::NeedDict
            && !self.dictionary().is_empty()
        {
            let dict = self.dictionary();
            self.err = c::inflateSetDictionary(
                &mut self.state,
                dict.as_ptr(),
                u32::try_from(dict.len()).unwrap(),
            );

            if self.err == c::ReturnCode::Ok {
                self.err = c::inflate(&mut self.state, self.flush);
            } else if self.err == c::ReturnCode::DataError {
                self.err = c::ReturnCode::NeedDict;
            }
        }
        while self.state.avail_in > 0
            && self.mode == c::NodeMode::GUNZIP
            && self.err == c::ReturnCode::StreamEnd
            // SAFETY: avail_in > 0 ⇒ next_in points to ≥1 readable byte.
            && unsafe { *self.state.next_in } != 0
        {
            // Bytes remain in input buffer. Perhaps this is another compressed member in the same archive, or just trailing garbage.
            // Trailing zero bytes are okay, though, since they are frequently used for padding.
            let _ = self.reset();
            self.err = c::inflate(&mut self.state, self.flush);
        }
    }

    pub fn update_write_result(&self, avail_in: &mut u32, avail_out: &mut u32) {
        *avail_in = self.state.avail_in;
        *avail_out = self.state.avail_out;
    }

    pub fn get_error_info(&self) -> Error {
        match self.err {
            c::ReturnCode::Ok | c::ReturnCode::BufError => {
                if self.state.avail_out != 0 && self.flush == c::FlushValue::Finish {
                    return self.error_for_message(c"unexpected end of file");
                }
            }
            c::ReturnCode::StreamEnd => {}
            c::ReturnCode::NeedDict => {
                if self.dictionary().is_empty() {
                    return self.error_for_message(c"Missing dictionary");
                } else {
                    return self.error_for_message(c"Bad dictionary");
                }
            }
            _ => {
                return self.error_for_message(c"Zlib error");
            }
        }
        Error::ok()
    }

    pub fn close(&mut self) {
        use c::NodeMode::*;
        let mut status = c::ReturnCode::Ok;
        match self.mode {
            DEFLATE | DEFLATERAW | GZIP => {
                status = c::deflateEnd(&mut self.state);
            }
            INFLATE | INFLATERAW | GUNZIP | UNZIP => {
                status = c::inflateEnd(&mut self.state);
            }
            NONE => {}
            BROTLI_ENCODE | BROTLI_DECODE => {}
            ZSTD_COMPRESS | ZSTD_DECOMPRESS => {}
        }
        debug_assert!(status == c::ReturnCode::Ok || status == c::ReturnCode::DataError);
        self.mode = NONE;
    }
}

