impl Response {
    #[inline]
    pub fn get_body_len(&mut self) -> usize {
        self.body.len() as usize
    }

    pub fn get_form_data_encoding(&mut self) -> JsResult<Option<Box<bun_core::form_data::AsyncFormData>>> {
        let Some(content_type_slice) = self.get_content_type()? else {
            return Ok(None);
        };
        // content_type_slice drops at scope exit
        let Some(encoding) = bun_core::form_data::Encoding::get(content_type_slice.slice()) else {
            return Ok(None);
        };
        Ok(Some(bun_core::form_data::AsyncFormData::init(encoding)))
    }

    pub fn calculate_estimated_byte_size(&mut self) {
        self.reported_estimated_size = self.body.value.estimated_size()
            + self.url.byte_slice().len()
            + self.init.status_text.byte_slice().len()
            + mem::size_of::<Response>();
    }

    fn check_body_stream_ref(&mut self, global_object: &JSGlobalObject) {
        if let Some(js_value) = self.js_ref.try_get() {
            if let BodyValue::Locked(locked) = &mut self.body.value {
                if let Some(stream) = locked.readable.get(global_object) {
                    // we dont hold a strong reference to the stream we will guard it in js.gc.stream
                    // so we avoid cycled references
                    // anyone using Response should not use Locked.readable directly because it dont always owns it
                    // the owner will be always the Response object it self
                    stream.value.ensure_still_alive();
                    js::stream_set_cached(js_value, global_object, stream.value);
                    // old readable dropped; reset to empty
                    locked.readable = Default::default();
                }
            }
        }
    }

    pub fn to_js(&mut self, global_object: &JSGlobalObject) -> JSValue {
        self.calculate_estimated_byte_size();
        // `bun_jsc::generated::JSResponse::to_js` ⇒ `Response__create` (C++
        // shim). Payload type is erased (`*mut ()`) at the bun_jsc tier.
        let js_value = js::to_js(self as *mut Self as *mut (), global_object);
        self.js_ref = JsRef::init_weak(js_value);

        self.check_body_stream_ref(global_object);
        js_value
    }

    #[inline]
    pub fn get_body_readable_stream(&mut self, global_object: &JSGlobalObject) -> Option<ReadableStream> {
        if let Some(js_ref) = self.js_ref.try_get() {
            if let Some(stream) = js::stream_get_cached(js_ref) {
                // JS is always source of truth for the stream
                return match ReadableStream::from_js(stream, global_object) {
                    Ok(rs) => rs,
                    Err(err) => {
                        let _ = global_object.take_exception(err);
                        None
                    }
                };
            }
        }
        if let BodyValue::Locked(locked) = &self.body.value {
            return locked.readable.get(global_object);
        }
        None
    }

    #[inline]
    pub fn detach_readable_stream(&mut self, global_object: &JSGlobalObject) {
        if let Some(js_ref) = self.js_ref.try_get() {
            // Zig `js.gc.stream.clear` ⇒ `set(.., .zero)`.
            js::stream_set_cached(js_ref, global_object, JSValue::ZERO);
        }
        if let BodyValue::Locked(locked) = &mut self.body.value {
            // old readable dropped; reset to empty
            locked.readable = Default::default();
        }
    }

    #[inline]
    pub fn set_size_hint(&mut self, size_hint: super::blob::SizeType) {
        if let BodyValue::Locked(locked) = &mut self.body.value {
            locked.size_hint = size_hint;
            // SAFETY: `locked.global` is set from a live `&JSGlobalObject` and is non-null
            // for the lifetime of a Locked body.
            if let Some(readable) = locked.readable.get(unsafe { &*locked.global }) {
                if let super::readable_stream::Source::Bytes(bytes) = &readable.ptr {
                    // TODO(port): lifetime — readable.ptr.Bytes is a back-pointer; verify mutability
                    // SAFETY: `bytes` is a live `*mut ByteStream` back-pointer owned by the
                    // ReadableStream; mutating its `size_hint` is the Zig contract.
                    unsafe { (**bytes).size_hint = size_hint; }
                }
            }
        }
    }
}

