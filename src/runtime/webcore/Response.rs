            writer.write_str(Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>statusText<d>:<r> "))?;
            write!(writer, "{}", Output::pretty_args::<ENABLE_ANSI_COLORS>("<r>\"<b>{}<r>\"", &self.init.status_text))?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
