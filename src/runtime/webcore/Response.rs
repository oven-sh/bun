#[unsafe(export_name = "jsFunctionRequestOrResponseHasBodyValue")]
#[bun_jsc::host_call]
pub fn js_function_request_or_response_has_body_value(_global: *mut JSGlobalObject, callframe: &CallFrame) -> JSValue {
    let arguments = callframe.arguments_old::<1>();
    let this_value = arguments.ptr[0];
    if this_value.is_empty_or_undefined_or_null() {
        return JSValue::FALSE;
    }

    if let Some(response) = this_value.as_::<Response>() {
        // SAFETY: `as_` returned a live `*mut Response` owned by the JS wrapper.
        // R-2: deref as shared — `Body.value` is `JsCell`.
        return JSValue::from(!unsafe { &*response }.body.get().value.get().is_definitely_empty());
    } else if let Some(request) = this_value.as_::<Request>() {
        // SAFETY: `as_` returned a live `*mut Request` owned by the JS wrapper.
        // R-2: deref as shared — `get_body_value` takes `&self`.
        return JSValue::from(!unsafe { &*request }.get_body_value().is_definitely_empty());
    }

    JSValue::FALSE
}

#[unsafe(export_name = "jsFunctionGetCompleteRequestOrResponseBodyValueAsArrayBuffer")]
#[bun_jsc::host_call]
pub fn js_function_get_complete_request_or_response_body_value_as_array_buffer(
    global_object: *mut JSGlobalObject,
    callframe: *mut CallFrame,
) -> JSValue {
    // SAFETY: JSC passes live global/callframe.
    let (global_object, callframe) = unsafe { (&*global_object, &*callframe) };
    let arguments = callframe.arguments_old::<1>();
    let this_value = arguments.ptr[0];
    if this_value.is_empty_or_undefined_or_null() {
        return JSValue::UNDEFINED;
    }

    let body: &mut BodyValue = 'brk: {
        if let Some(response) = this_value.as_::<Response>() {
            // SAFETY: `as_` returned a live `*mut Response` owned by the JS wrapper.
            // R-2: deref as shared; `get_body_value` projects `&mut` via `JsCell`.
            break 'brk unsafe { &*response }.get_body_value();
        } else if let Some(request) = this_value.as_::<Request>() {
            // SAFETY: `as_` returned a live `*mut Request` owned by the JS wrapper.
            // R-2: deref as shared — `get_body_value` takes `&self`.
            break 'brk unsafe { &*request }.get_body_value();
        }

        return JSValue::UNDEFINED;
    };

    // Get the body if it's available synchronously.
    match body {
        BodyValue::Used | BodyValue::Empty | BodyValue::Null => JSValue::UNDEFINED,
        BodyValue::Blob(blob) => {
            if blob.is_bun_file() {
                return JSValue::UNDEFINED;
            }
            let result = match blob.to_array_buffer(global_object, crate::webcore::Lifetime::Transfer) {
                Ok(v) => v,
                Err(_) => JSValue::ZERO,
            };
            *body = BodyValue::Used;
            result
        }
        BodyValue::WTFStringImpl(_) | BodyValue::InternalBlob(_) => {
            let mut any_blob = body.use_as_any_blob();
            match any_blob.to_array_buffer_transfer(global_object) {
                Ok(v) => v,
                Err(_) => JSValue::ZERO,
            }
        }
        BodyValue::Error(_) | BodyValue::Locked(_) => JSValue::UNDEFINED,
    }
}

