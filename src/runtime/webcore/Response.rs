    pub fn write_format<F, W, const ENABLE_ANSI_COLORS: bool>(
        &mut self,
        formatter: &mut F,
        writer: &mut W,
    ) -> core::fmt::Result
    where
        F: bun_jsc::ConsoleFormatter,
        W: core::fmt::Write,
    {
        // PORT NOTE: return type narrowed to `core::fmt::Result`. The trait
        // methods produce `fmt::Error`/`JsError`/`bun_core::Error`; none of
        // those convert into the others, so funnel everything through
        // `fmt::Error` (Zig's `anyerror!void` carried no payload either).
        let js_err = |_: JsError| core::fmt::Error;

        write!(writer, "Response ({}) {{\n", bun_core::fmt::size(self.body.len() as usize, Default::default()))?;

        {
            formatter.indent_inc();
            // Zig: `defer formatter.indent -|= 1;` — must run on every exit incl. `?` error paths.
            // SAFETY: `formatter` outlives `_indent_guard` (same scope, guard dropped first);
            // the raw pointer is only dereferenced in the closure at scope exit, at which point
            // no other borrow of `formatter` is live.
            let _indent_guard = scopeguard::guard(
                formatter as *mut F,
                |p| unsafe { (*p).indent_dec() },
            );

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>ok<d>:<r> "))?;
            formatter.print_as::<_, ENABLE_ANSI_COLORS>(bun_jsc::FormatAs::Boolean, writer, JSValue::from(self.is_ok()), bun_jsc::JSType::BooleanObject).map_err(js_err)?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>url<d>:<r> \""))?;
            write!(writer, "{}", Output::pretty_fmt_args("<r><b>{}<r>", ENABLE_ANSI_COLORS, (&self.url,)))?;
            writer.write_str("\"")?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>status<d>:<r> "))?;
            formatter.print_as::<_, ENABLE_ANSI_COLORS>(bun_jsc::FormatAs::Double, writer, JSValue::js_number(self.init.status_code as f64), bun_jsc::JSType::NumberObject).map_err(js_err)?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>statusText<d>:<r> "))?;
            write!(writer, "{}", Output::pretty_fmt_args("<r>\"<b>{}<r>\"", ENABLE_ANSI_COLORS, (&self.init.status_text,)))?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>headers<d>:<r> "))?;
            let headers_js = Self::get_headers(self, formatter.global_this()).map_err(js_err)?;
            formatter.print_as::<_, ENABLE_ANSI_COLORS>(bun_jsc::FormatAs::Private, writer, headers_js, bun_jsc::JSType::DOMWrapper).map_err(js_err)?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.write_indent(writer)?;
            write!(writer, "{}", Output::pretty_fmt::<ENABLE_ANSI_COLORS>("<r>redirected<d>:<r> "))?;
            formatter.print_as::<_, ENABLE_ANSI_COLORS>(bun_jsc::FormatAs::Boolean, writer, JSValue::from(self.redirected), bun_jsc::JSType::BooleanObject).map_err(js_err)?;
            formatter.print_comma::<_, ENABLE_ANSI_COLORS>(writer)?;
            writer.write_str("\n")?;

            formatter.reset_line();
            self.body.write_format::<F, W, ENABLE_ANSI_COLORS>(formatter, writer)?;
            // indent restored by `_indent_guard` on scope exit (incl. error returns)
        }
        writer.write_str("\n")?;
        formatter.write_indent(writer)?;
        writer.write_str("}")?;
        formatter.reset_line();
        Ok(())
    }

    // TODO(b2-blocked): #[bun_jsc::host_fn(method)]
    pub fn do_clone(this: &mut Self, global_this: &JSGlobalObject, callframe: &CallFrame) -> JsResult<JSValue> {
        let this_value = callframe.this();
        let cloned = this.clone(global_this)?;

        let js_wrapper = Response::make_maybe_pooled(global_this, cloned);

        if !js_wrapper.is_empty() {
            // After toJS/makeMaybePooled, checkBodyStreamRef has already moved
            // the streams from Locked.readable to js.gc.stream. So we need to
            // use js.gc.stream to get the streams and update the body cache.
            if let Some(cloned_stream) = js::stream_get_cached(js_wrapper) {
                js::body_set_cached(js_wrapper, global_this, cloned_stream);
            }
        }

        // Update the original response's body cache with the new teed stream.
        // At this point, this.body.value.Locked.readable still holds the teed stream
        // because checkBodyStreamRef hasn't been called on the original response yet.
        if let BodyValue::Locked(locked) = &this.body.value {
            if let Some(readable) = locked.readable.get(global_this) {
                js::body_set_cached(this_value, global_this, readable.value);
            }
        }

        this.check_body_stream_ref(global_this);

        Ok(js_wrapper)
    }

    pub fn make_maybe_pooled(global_object: &JSGlobalObject, ptr: *mut Response) -> JSValue {
        // SAFETY: ptr is a freshly-boxed Response from clone()
        unsafe { (*ptr).to_js(global_object) }
    }

    pub fn clone_value(&mut self, global_this: &JSGlobalObject) -> JsResult<Response> {
        let body = 'brk: {
            if let Some(js_ref) = self.js_ref.try_get() {
                if let Some(stream) = js::stream_get_cached(js_ref) {
                    let mut readable = ReadableStream::from_js(stream, global_this)?;
                    if let Some(readable) = readable.as_mut() {
                        break 'brk self.body.clone_with_readable_stream(global_this, Some(readable))?;
                    }
                }
            }

            self.body.clone(global_this)?
        };
        // errdefer body.deinit() — `Body` has NO `Drop`; arm a guard so the
        // `?` below releases the cloned body payload (Response.zig:433).
        let body = scopeguard::guard(body, |mut b| b.reset());
        let init = self.init.clone(global_this)?;
        // errdefer init.deinit() — Init's drop glue (HeadersRef + OwnedString)
        // handles cleanup on `?` below
        Ok(Response {
            body: scopeguard::ScopeGuard::into_inner(body),
            init,
            url: OwnedString::new(self.url.clone()),
            redirected: self.redirected,
            ..Default::default()
        })
    }

    pub fn clone(&mut self, global_this: &JSGlobalObject) -> JsResult<*mut Response> {
        Ok(Box::into_raw(Box::new(self.clone_value(global_this)?)))
    }

    fn destroy(this: *mut Response) {
        // SAFETY: called from unref() when ref_count hits 0; this is the unique owner
        unsafe {
            // Mirrors Zig `destroy` (Response.zig:457-460): `init.deinit()` /
            // `body.deinit()` / `url.deref()` / `js_ref.deinit()`.
            //
            // We assign safe-empty values rather than `drop_in_place` so the
            // struct stays in a valid (all-empty) state if `on_finalize()`
            // returns false and the allocation outlives this call until the
            // last WeakRef releases it.
            //
            // - `Init` field drop glue releases `headers` (HeadersRef::Drop →
            //   C++ deref) and `status_text` (OwnedString::Drop → WTF deref).
            // - `Body` has NO `Drop`; `reset()` is the explicit cleanup API
            //   (Body.rs renames `deinit` → `reset`). `drop_in_place` here
            //   would leak refcounted payloads (WTFStringImpl, Blob store).
            // - `url: OwnedString` — assignment drops the old value (WTF deref).
            // - `JsRef` — assignment drops the `Strong` arm (HandleSlot freed).
            (*this).init = Init::default();
            (*this).body.reset();
            (*this).url = OwnedString::new(BunString::empty());
            (*this).js_ref = JsRef::empty();

            // Contents are gone; the allocation itself stays until any outstanding
            // WeakRef derefs (RequestContext.response_weakref). WeakRef.get() returns
            // null from here on.
            if (*this).weak_ptr_data.on_finalize() {
                // Do NOT use Box::from_raw — that would re-run field drop glue
                // on init/url/js_ref. They are now safe-empty so the second drop
                // would be a no-op, but it is still wasted work and fragile under
                // future field additions. Zig's `bun.destroy()` only frees the
                // allocation; match that with a raw dealloc.
                let layout = std::alloc::Layout::new::<Response>();
                std::alloc::dealloc(this as *mut u8, layout);
            }
        }
    }

    pub fn ref_(this: *mut Response) -> *mut Response {
        // SAFETY: intrusive refcount; caller holds a live reference
        unsafe {
            (*this).ref_count += 1;
        }
        this
    }

    pub fn unref(this: *mut Response) {
        // SAFETY: intrusive refcount; caller holds a live reference
        unsafe {
            debug_assert!((*this).ref_count > 0);
            (*this).ref_count -= 1;
            if (*this).ref_count == 0 {
                Self::destroy(this);
            }
        }
    }

    pub fn finalize(this: *mut Response) {
        // SAFETY: called by JSC codegen on mutator thread during lazy sweep
        unsafe {
            (*this).js_ref.finalize();
        }
        Self::unref(this);
    }

    // TODO(b2-blocked): #[bun_jsc::host_fn]
    pub fn construct_json(global_this: &JSGlobalObject, callframe: &CallFrame) -> JsResult<JSValue> {
        let args_list = callframe.arguments_old::<2>();
        // https://github.com/remix-run/remix/blob/db2c31f64affb2095e4286b91306b96435967969/packages/remix-server-runtime/responses.ts#L4
        // SAFETY: `bun_vm()` returns a raw `*mut VirtualMachine` (PORTING.md
        // §raw-ptr) — borrow it for the duration of args parsing.
        let mut args = bun_jsc::ArgumentsSlice::init(unsafe { &*global_this.bun_vm() }, &args_list.ptr[0..args_list.len]);

        let mut response = Response {
            body: Body { value: BodyValue::Empty },
            init: Init { status_code: 200, ..Default::default() },
            ..Default::default()
        };
        // PORT NOTE: Zig tracked `did_succeed` and manually deinit'd body/init
        // on failure. `Init`'s field drop glue (HeadersRef + OwnedString)
        // releases its refs on `?`. `Body` has NO `Drop`, but the only
        // payloads it can hold here are `Empty` / `InternalBlob{Vec}` /
        // `WTFStringImpl{Arc}` whose own field drop glue frees the buffers; so
        // dropping `response` on `?` matches Zig's cleanup. On success the
        // value is moved into `Box::new` and not dropped here.
        let json_value = args.next_eat().unwrap_or(JSValue::ZERO);

        if !json_value.is_empty() {
            // Validate top-level values that are not JSON serializable (Node.js compatibility)
            if json_value.is_undefined() || json_value.is_symbol() || json_value.js_type() == bun_jsc::JSType::JSFunction {
                let err = global_this.create_type_error_instance(format_args!("Value is not JSON serializable"));
                return Err(global_this.throw_value(err));
            }

            // BigInt has a different error message to match Node.js exactly
            if json_value.is_big_int() {
                let err = global_this.create_type_error_instance(format_args!("Do not know how to serialize a BigInt"));
                return Err(global_this.throw_value(err));
            }

            let mut str = BunString::empty();
            // Use jsonStringifyFast which passes undefined for the space parameter,
            // triggering JSC's FastStringifier optimization. This is significantly faster
            // than jsonStringify which passes 0 for space and uses the slower Stringifier.
            json_value.json_stringify_fast(global_this, &mut str)?;

            if global_this.has_exception() {
                return Ok(JSValue::ZERO);
            }

            if !str.is_empty() {
                // PORT NOTE: `bun_str::String.value` is private; use
                // `leak_wtf_impl()` to take ownership of the +1 ref as a raw
                // `*mut WTFStringImplStruct`. `String` is intentionally
                // non-`Drop`, so consuming it here is the same as Zig's
                // bitwise field read.
                let wtf = str.leak_wtf_impl();
                debug_assert!(!wtf.is_null());
                // SAFETY: `json_stringify_fast` populated a WTF-backed string;
                // `wtf` is a live impl pointer until we deref it below.
                if let Some(bytes) = unsafe { (*wtf).to_utf8_if_needed() } {
                    // We took +1 via leak_wtf_impl; release it now (Zig: `defer str.deref()`).
                    unsafe { (*wtf).deref() };
                    response.body.value = BodyValue::InternalBlob(InternalBlob {
                        // TODO(port): Zig used Managed(u8).fromOwnedSlice; bytes.slice() ownership
                        // transfers here as Vec<u8>.
                        bytes: bytes.into_vec(),
                        was_string: true,
                    });
                } else {
                    // Zig moves the WTFStringImpl pointer bitwise (no ref/deref).
                    // We already hold the +1 from `leak_wtf_impl`; transfer it
                    // into the body value.
                    response.body.value = BodyValue::WTFStringImpl(wtf);
                }
            }
        }

        if let Some(arg_init) = args.next_eat() {
            if arg_init.is_undefined_or_null() {
                // no-op
            } else if arg_init.is_number() {
                response.init.status_code =
                    u16::try_from(0.max(arg_init.to_int32()).min(i32::from(u16::MAX))).unwrap();
            } else {
                match Init::init(global_this, arg_init) {
                    Ok(Some(init)) => response.init = init,
                    Ok(None) => {}
                    Err(e) if e == bun_jsc::JsError::Thrown => return Ok(JSValue::ZERO),
                    Err(_) => {}
                }
            }
        }

        let headers_ref = response.get_or_create_headers(global_this)?;
        let json_mime = bun_http_types::MimeType::JSON;
        headers_ref.put_default(HTTPHeaderName::ContentType, json_mime.value.as_ref(), global_this)?;
        Ok(Box::leak(Box::new(response)).to_js(global_this))
    }

    fn validate_redirect_status_code(global_this: &JSGlobalObject, status_code: i32) -> JsResult<u16> {
        match status_code {
            301 | 302 | 303 | 307 | 308 => Ok(u16::try_from(status_code).unwrap()),
            _ => {
                let err = global_this.create_range_error_instance(format_args!(
                    "Failed to execute 'redirect' on 'Response': Invalid status code"
                ));
                Err(global_this.throw_value(err))
            }
        }
    }

