    pub fn construct_redirect_impl(global_this: &JSGlobalObject, callframe: &CallFrame) -> JsResult<Response> {
        let args_list = callframe.arguments_old(4);
        // https://github.com/remix-run/remix/blob/db2c31f64affb2095e4286b91306b96435967969/packages/remix-server-runtime/responses.ts#L4
        let mut args = bun_jsc::ArgumentsSlice::init(global_this.bun_vm(), &args_list.ptr[0..args_list.len]);

        let mut url_string_slice = ZigString::Slice::empty();
        // url_string_slice drops at scope exit
        let mut response: Response = 'brk: {
            let mut response = Response {
                init: Init { status_code: 302, ..Default::default() },
                body: Body { value: BodyValue::Empty },
                url: BunString::empty(),
                ..Default::default()
            };

            let url_string_value = args.next_eat().unwrap_or(JSValue::ZERO);
            let mut url_string = ZigString::init(b"");

            if !url_string_value.is_empty() {
                url_string = url_string_value.get_zig_string(global_this)?;
            }
            url_string_slice = url_string.to_slice();
            // PORT NOTE: Zig tracked `did_succeed` and manually deinit'd body/init on
            // failure. In Rust, Drop handles cleanup on `?`.

            if let Some(arg_init) = args.next_eat() {
                if arg_init.is_undefined_or_null() {
                    // no-op
                } else if arg_init.is_number() {
                    response.init.status_code = Self::validate_redirect_status_code(global_this, arg_init.to_int32())?;
                } else if let Some(init) = Init::init(global_this, arg_init)? {
                    // errdefer response.init.deinit() — handled by Drop on `?` below
                    response.init = init;

                    if response.init.status_code != 200 {
                        response.init.status_code =
                            Self::validate_redirect_status_code(global_this, i32::from(response.init.status_code))?;
                    }
                }
            }

            if global_this.has_exception() {
                return Err(bun_jsc::JsError::Thrown);
            }
            break 'brk response;
        };

        let headers = response.get_or_create_headers(global_this)?;
        // PORT NOTE: reshaped for borrowck — Zig assigned response.init.headers = ...
        // then re-borrowed; here get_or_create_headers already populated init.headers.
        headers.put(FetchHeaders::HTTPHeaderName::Location, url_string_slice.slice(), global_this)?;
        Ok(response)
    }

