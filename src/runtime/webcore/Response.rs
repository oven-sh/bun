    pub fn get_response_type(this: &Self, global_this: &JSGlobalObject) -> JsResult<JSValue> {
        if this.init.get().status_code < 200 {
            return Ok(global_this.common_strings().error());
        }

        Ok(global_this.common_strings().default())
    }

