pub struct PostgresSQLQuery {
    pub statement: Option<*mut PostgresSQLStatement>,
    pub query: BunString,
    pub cursor_name: BunString,

    pub this_value: JsRef,

    pub status: Status,

    // bun.ptr.RefCount(@This(), "ref_count", deinit, .{}) — intrusive single-thread refcount.
    // TODO(port): IntrusiveRc<PostgresSQLQuery>; ref_/deref_ below manipulate this directly
    // because *PostgresSQLQuery is stored raw in PostgresSQLConnection.requests.
    ref_count: Cell<u32>,

    pub flags: Flags,
}

// Zig `deinit`: `if (this.statement) |s| s.deref()` then deref query/cursor_name,
// then `destroy(this)`. `query`/`cursor_name` are `BunString` (already Drop) and
// `destroy` is `Box::from_raw` in `deref_`; only the raw `*mut PostgresSQLStatement`
// needs an explicit deref here.
impl Drop for PostgresSQLQuery {
    fn drop(&mut self) {
        if let Some(stmt) = self.statement.take() {
            // SAFETY: `stmt` was produced by `Box::into_raw` (in `do_run` or
            // PostgresSQLConnection's statements map) and was ref'd when stored
            // into `self.statement`.
            unsafe { PostgresSQLStatement::deref(stmt) };
        }
    }
}

impl Default for PostgresSQLQuery {
    fn default() -> Self {
        Self {
            statement: None,
            query: BunString::empty(),
            cursor_name: BunString::empty(),
            this_value: JsRef::empty(),
            status: Status::Pending,
            ref_count: Cell::new(1),
            flags: Flags::default(),
        }
    }
}

// PORT NOTE: Zig used `packed struct(u8)`. Ported as a plain struct with public
// fields because `PostgresSQLConnection.rs` reads/writes these directly
// (`req.flags.simple`, `req.flags.binary = ...`, `req.flags.result_mode`).
