//! Generator binary for grapheme property tables.
//! Invoked via `zig build generate-grapheme-tables`.
//! Queries uucode for each codepoint and outputs a source file
//! containing the pre-generated 3-level lookup tables.
//!
//! The table stores only GraphemeBreakNoControl values (u5, 17 possible),
//! keeping the tables small through high deduplication.
//!
//! NOTE: this is a **build-time binary**, not runtime code — it is allowed to
//! touch `std::io::stdout()` directly.

use std::io::{self, BufWriter, Write};

use crate::uucode::lut;
// TODO(port): `uucode` is the vendored Zig dep at src/deps/uucode/ — Phase B
// must decide whether to wrap it via FFI or replace with a Rust UCD source.
use uucode;

/// Must be kept in sync with grapheme.zig's GraphemeBreakNoControl.
// Zig: enum(u5). Rust has no u5; smallest fitting repr is u8.
#[repr(u8)]
#[derive(Copy, Clone, PartialEq, Eq, strum::IntoStaticStr)]
#[strum(serialize_all = "snake_case")]
pub enum GraphemeBreakNoControl {
    Other,
    Prepend,
    RegionalIndicator,
    SpacingMark,
    L,
    V,
    T,
    Lv,
    Lvt,
    Zwj,
    Zwnj,
    ExtendedPictographic,
    EmojiModifierBase,
    EmojiModifier,
    IndicConjunctBreakExtend,
    IndicConjunctBreakLinker,
    IndicConjunctBreakConsonant,
}

pub struct Context;

impl Context {
    pub fn get(&self, cp: u32) -> Result<GraphemeBreakNoControl, bun_core::Error> {
        if cp > uucode::config::MAX_CODE_POINT {
            return Ok(GraphemeBreakNoControl::Other);
        }

        let uu_gb = uucode::get(uucode::Field::GraphemeBreakNoControl, cp);
        // SAFETY: GraphemeBreakNoControl has the same discriminants as the
        // uucode grapheme_break_no_control enum (asserted by the doc comment
        // "Must be kept in sync").
        Ok(unsafe { core::mem::transmute::<u8, GraphemeBreakNoControl>(uu_gb as u8) })
    }

    pub fn eql(&self, a: GraphemeBreakNoControl, b: GraphemeBreakNoControl) -> bool {
        a == b
    }
}

pub fn main() -> Result<(), bun_core::Error> {
    // PERF(port): was ArenaAllocator bulk-free — build-time tool, irrelevant.

    let gen: lut::Generator<GraphemeBreakNoControl, Context> = Default::default();

    let t = gen.generate()?;
    // (Zig had `defer alloc.free(t.stageN)` — t owns its stages now and Drops them.)

    let stdout_file = io::stdout();
    let mut stdout = BufWriter::with_capacity(4096, stdout_file.lock());

    // Write header
    // TODO(port): output format below emits Zig source. Phase B must retarget
    // this to emit Rust source for `grapheme_tables.rs` (the generated stub).
    stdout
        .write_all(
            b"//! This file is auto-generated by `zig build generate-grapheme-tables`.\n\
              //! Do not edit manually.\n\
              \n\
              const grapheme = @import(\"./grapheme.zig\");\n\
              const GraphemeBreakNoControl = grapheme.GraphemeBreakNoControl;\n\
              \n\
              pub const table = grapheme.Tables(GraphemeBreakNoControl){\n\
              \x20   .stage1 = &stage1,\n\
              \x20   .stage2 = &stage2,\n\
              \x20   .stage3 = &stage3,\n\
              };\n\
              \n",
        )
        .map_err(|_| bun_core::err!("WriteFailed"))?;

    // Write stage1
    write!(stdout, "const stage1: [{}]u16 = .{{", t.stage1.len())
        .map_err(|_| bun_core::err!("WriteFailed"))?;
    for entry in t.stage1.iter() {
        write!(stdout, "{},", entry).map_err(|_| bun_core::err!("WriteFailed"))?;
    }
    stdout
        .write_all(b"};\n\n")
        .map_err(|_| bun_core::err!("WriteFailed"))?;

    // Write stage2
    write!(stdout, "const stage2: [{}]u8 = .{{", t.stage2.len())
        .map_err(|_| bun_core::err!("WriteFailed"))?;
    for entry in t.stage2.iter() {
        write!(stdout, "{},", entry).map_err(|_| bun_core::err!("WriteFailed"))?;
    }
    stdout
        .write_all(b"};\n\n")
        .map_err(|_| bun_core::err!("WriteFailed"))?;

    // Write stage3
    write!(
        stdout,
        "const stage3: [{}]GraphemeBreakNoControl = .{{",
        t.stage3.len()
    )
    .map_err(|_| bun_core::err!("WriteFailed"))?;
    for entry in t.stage3.iter() {
        write!(stdout, ".{},", <&'static str>::from(*entry))
            .map_err(|_| bun_core::err!("WriteFailed"))?;
    }
    stdout
        .write_all(b"};\n")
        .map_err(|_| bun_core::err!("WriteFailed"))?;
    stdout.flush().map_err(|_| bun_core::err!("WriteFailed"))?;

    Ok(())
}

