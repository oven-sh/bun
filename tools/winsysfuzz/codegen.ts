// Parses NtTrace.cfg (rogerorr/NtTrace, MIT) — a SAL-annotated database of
// ntdll's native syscall prototypes — and generates the Detours hook table.
//
// The cfg is the syscall grammar we never have to hand-write: 537 Nt* entry
// points, each argument tagged _In_/_Out_/_In_opt_/... . We only need the
// *arity* to build a faithful x64 trampoline (every argument is one 8-byte
// slot); the annotations and type names feed the trace pretty-printer and the
// hostile-argument mutator's targeting.
//
// Emits:
//   src/generated/hooks.gen.h    — SysId enum, kSyscalls[] metadata, hook decls
//   src/generated/hooks.gen.cpp  — one typed detour + Real_ pointer per syscall
//   driver/generated/syscalls.gen.json — same table for the JS controller
//
// Run: bun tools/winsysfuzz/codegen.ts

import { mkdirSync } from "node:fs";
import { dirname, join } from "node:path";

const here = dirname(import.meta.path);
const cfgPath = join(here, "NtTrace.cfg");

type Dir = "in" | "out" | "inout";
interface Arg {
  name: string;
  type: string; // C type as written (e.g. "PHANDLE", "PLARGE_INTEGER")
  dir: Dir;
  opt: boolean; // _*_opt_ — pointer may legally be null
  sal: string; // raw SAL token, kept for the arg mutator (e.g. _Out_writes_bytes_(Length))
}
interface Syscall {
  name: string;
  ret: string; // "NTSTATUS" for 532/537; ULONG or a pointer otherwise
  category: string; // NtTrace's grouping: File, Registry, LPC, Process, ...
  args: Arg[];
}

function parseDir(sal: string): { dir: Dir; opt: boolean } {
  const s = sal.toLowerCase();
  const opt = s.includes("opt");
  const isIn = s.includes("in");
  const isOut = s.includes("out");
  if (isIn && isOut) return { dir: "inout", opt };
  if (isOut) return { dir: "out", opt };
  return { dir: "in", opt };
}

function parse(text: string): Syscall[] {
  const lines = text.split(/\r?\n/);
  const out: Syscall[] = [];
  let category = "";
  let pendingRet = "";
  for (let i = 0; i < lines.length; i++) {
    const raw = lines[i];
    const line = raw.trim();
    // //[Category] tags the next entry; NtTrace uses [-Other] for its
    // catch-all — normalize the dash away.
    const cat = /^\/\/\s*\[-?([A-Za-z0-9_ ]+)\]/.exec(line);
    if (cat) {
      category = cat[1];
      continue;
    }
    if (line === "" || line.startsWith("//") || line.startsWith("#") || line.startsWith("using ")) continue;
    // Return-type line precedes NTAPI + the entry. Anything that is a bare
    // type token (no parens/commas) and not NTAPI itself.
    if (/^[A-Za-z_][A-Za-z0-9_ *]*$/.test(line) && line !== "NTAPI") {
      pendingRet = line.replace(/\s+/g, " ");
      continue;
    }
    if (line === "NTAPI") continue;
    const m = /^(Nt[A-Za-z0-9_]+)\(\s*$/.exec(line);
    if (!m) continue;
    const name = m[1];
    const args: Arg[] = [];
    // Consume argument lines until the closing ");".
    for (i = i + 1; i < lines.length; i++) {
      let a = lines[i].replace(/\/\/.*$/, "").trim();
      if (a.startsWith(");")) break;
      if (a === "") continue;
      a = a.replace(/,$/, "").trim();
      if (a === "VOID" || a === "void") continue;
      // Tokens: [SAL] type... name. SAL tokens begin with "_" and may carry
      // a parenthesized parameter with no internal spaces.
      const toks = a.split(/\s+/);
      let sal = "";
      if (toks[0].startsWith("_")) sal = toks.shift()!;
      if (toks.length < 2) continue;
      const argName = toks.pop()!.replace(/[\[\]]/g, "");
      const type = toks.join(" ");
      const { dir, opt } = parseDir(sal);
      args.push({ name: argName, type, dir, opt, sal });
    }
    out.push({ name, ret: pendingRet || "NTSTATUS", category, args });
    pendingRet = "";
  }
  return out;
}

const syscalls = parse(await Bun.file(cfgPath).text());

// Syscalls that do not return to their caller. A CallCtx held across such a
// call never destructs, leaking the reentrancy depth and silencing the
// thread forever (NtContinue ends every process's loader init on the main
// thread). These get an entry-only record with no context spanning the call.
const noReturn = new Set(["NtContinue", "NtCallbackReturn", "NtRaiseException", "NtTerminateProcess", "NtTerminateThread"]);

// ---------------------------------------------------------------------------
// Emit C++.
// Every Nt* argument is exactly one 8-byte register/stack slot on x64, and
// forwarding EXTRA slots is harmless — the kernel ignores arguments beyond
// the syscall's true arity. So every trampoline forwards a fixed FWD slots
// regardless of the cfg's declared count: NtTrace.cfg's arity lags newer
// Windows builds (NtWaitForWorkViaWorkerFactory is 2 in the cfg, 5 in the
// kernel), and forwarding too FEW would let the kernel read garbage stack for
// the missing arguments (STATUS_ACCESS_VIOLATION spin). Declared argc/types
// still feed the metadata table for the logger and mutator.
const FWD = 18;

const H: string[] = [];
H.push("// GENERATED by tools/winsysfuzz/codegen.ts from NtTrace.cfg — do not edit.");
H.push("#pragma once");
H.push('#include "../common.h"');
H.push("");
H.push("namespace wsf {");
H.push("");
H.push("enum SysId : uint32_t {");
for (const s of syscalls) H.push(`  SYS_${s.name},`);
H.push("  SYS__COUNT,");
H.push("};");
H.push("");
H.push("// Hook entrypoint + slot for the resolved ntdll export, per syscall.");
H.push("struct HookEntry {");
H.push("  const char* name;");
H.push("  void** real;   // &Real_<name>, patched to the ntdll export at attach");
H.push("  void* detour;  // Hook_<name>");
H.push("  uint8_t argc;");
H.push("  bool ntstatus; // returns NTSTATUS => eligible for fault injection");
H.push("  const char* const* argTypes;");
H.push("  const char* const* argNames;");
H.push("  const uint8_t* argDir;   // 0=in 1=out 2=inout (bit 7 = optional)");
H.push("  const char* category;");
H.push("  int8_t iosbIndex; // arg index of the PIO_STATUS_BLOCK, -1 if none (mangle target)");
H.push("  int8_t oaIndex;   // arg index of the POBJECT_ATTRIBUTES, -1 if none (path decode)");
H.push("  int8_t ioctlIndex;  // arg index of an IoControlCode/FsControlCode, -1 if none");
H.push("  int8_t handleIndex; // arg index of the primary in-HANDLE (File/Socket/Key), -1 if none");
H.push("  int8_t lengthIndex; // arg index of a ULONG Length (bytes requested), -1 if none");
H.push("  int8_t hOutIndex;   // arg index of the PHANDLE created by an open/create, -1 if none");
H.push("};");
H.push("");
H.push("extern HookEntry kHooks[SYS__COUNT];");
H.push("");
H.push("} // namespace wsf");

const C: string[] = [];
C.push("// GENERATED by tools/winsysfuzz/codegen.ts from NtTrace.cfg — do not edit.");
C.push('#include "hooks.gen.h"');
C.push("");
C.push("namespace wsf {");
C.push("");
const dirCode: Record<Dir, number> = { in: 0, out: 1, inout: 2 };
const fwdParams = Array.from({ length: FWD }, (_, k) => `ULONG_PTR a${k}`).join(", ");
const fwdCall = Array.from({ length: FWD }, (_, k) => `a${k}`).join(", ");
for (const s of syscalls) {
  const n = s.args.length;
  const params = fwdParams;
  const call = fwdCall;
  const argsArr = n ? `{${Array.from({ length: n }, (_, k) => `a${k}`).join(", ")}}` : "{}";
  const isNt = s.ret === "NTSTATUS";
  C.push(`// ${s.category ? "[" + s.category + "] " : ""}${s.ret} ${s.name}(${n} args)`);
  C.push(`static const char* const ${s.name}_types[] = {${s.args.map(a => JSON.stringify(a.type)).join(", ")}${n ? "" : "nullptr"}};`);
  C.push(`static const char* const ${s.name}_names[] = {${s.args.map(a => JSON.stringify(a.name)).join(", ")}${n ? "" : "nullptr"}};`);
  C.push(`static const uint8_t ${s.name}_dirs[] = {${s.args.map(a => dirCode[a.dir] | (a.opt ? 0x80 : 0)).join(", ")}${n ? "" : "0"}};`);
  C.push(`static ULONG_PTR (NTAPI *Real_${s.name})(${params})${n ? "" : "" } = nullptr;`);
  C.push(`static ULONG_PTR NTAPI Hook_${s.name}(${params}) {`);
  if (noReturn.has(s.name)) {
    // Does not return: entry record only, no context spanning the call.
    C.push(`  LogEntryOnly(SYS_${s.name}, (uintptr_t)_ReturnAddress());`);
    C.push(`  return Real_${s.name}(${call});`);
  } else {
    C.push(`  ULONG_PTR args[] = ${n ? argsArr : "{0}"};`);
    C.push(`  CallCtx ctx(SYS_${s.name}, (uintptr_t)_ReturnAddress(), args, ${n});`);
    C.push(`  if (${isNt ? "ctx.PreFault()" : "false"}) return ctx.Ret();`);
    C.push(`  ULONG_PTR r = Real_${s.name}(${call});`);
    C.push(`  return ctx.Exit(r);`);
  }
  C.push(`}`);
  C.push("");
}
C.push("HookEntry kHooks[SYS__COUNT] = {");
for (const s of syscalls) {
  const iosb = s.args.findIndex(a => a.type === "PIO_STATUS_BLOCK");
  const oa = s.args.findIndex(a => a.type === "POBJECT_ATTRIBUTES");
  // Control codes, the primary input handle, and requested byte lengths are
  // named consistently in the cfg (IoControlCode / FsControlCode,
  // FileHandle / SocketHandle / KeyHandle / EventHandle, Length): richer,
  // typed trace context and the basis for handle-targeted faults.
  const ioctl = s.args.findIndex(a => /^(IoControlCode|FsControlCode)$/.test(a.name));
  const handle = s.args.findIndex(
    a => a.type === "HANDLE" && a.dir === "in" && /^(FileHandle|SocketHandle|Handle|KeyHandle|PortHandle)$/.test(a.name),
  );
  const length = s.args.findIndex(a => a.type === "ULONG" && a.name === "Length");
  const hOut = s.args.findIndex(a => a.type === "PHANDLE" && a.dir === "out");
  C.push(
    `  {${JSON.stringify(s.name)}, (void**)&Real_${s.name}, (void*)&Hook_${s.name}, ${s.args.length}, ` +
      `${s.ret === "NTSTATUS" ? "true" : "false"}, ${s.name}_types, ${s.name}_names, ${s.name}_dirs, ` +
      `${JSON.stringify(s.category)}, ${iosb}, ${oa}, ${ioctl}, ${handle}, ${length}, ${hOut}},`,
  );
}
C.push("};");
C.push("");
C.push("} // namespace wsf");

// ---------------------------------------------------------------------------
// Emit JSON manifest for the JS controller/driver.
const manifest = syscalls.map((s, id) => ({ id, ...s }));

const genDir = join(here, "src", "generated");
const drvDir = join(here, "driver", "generated");
mkdirSync(genDir, { recursive: true });
mkdirSync(drvDir, { recursive: true });
await Bun.write(join(genDir, "hooks.gen.h"), H.join("\n") + "\n");
await Bun.write(join(genDir, "hooks.gen.cpp"), C.join("\n") + "\n");
await Bun.write(join(drvDir, "syscalls.gen.json"), JSON.stringify(manifest, null, 1) + "\n");

const byRet = new Map<string, number>();
for (const s of syscalls) byRet.set(s.ret, (byRet.get(s.ret) ?? 0) + 1);
console.log(`winsysfuzz codegen: ${syscalls.length} syscalls`);
for (const [r, c] of byRet) console.log(`  ret ${r.padEnd(16)} ${c}`);
const maxArgc = Math.max(...syscalls.map(s => s.args.length));
console.log(`  max argc ${maxArgc}; categories ${new Set(syscalls.map(s => s.category)).size}`);
